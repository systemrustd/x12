fn handle_put_image(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let Some(request) = x11::put_image_request(header.data, body) else {
        return Ok(RequestOutcome::Handled);
    };
    if request.width == 0 || request.height == 0 {
        return Ok(RequestOutcome::Handled);
    }
    debug!(
        "client {} #{} PutImage drawable=0x{:x} {}x{} dst=({},{}) depth={} fmt={:?}",
        client_id.0,
        sequence.0,
        request.drawable.0,
        request.width,
        request.height,
        request.dst_x,
        request.dst_y,
        request.depth,
        request.format,
    );
    let gc_exists = state.resources.gc(request.gc).is_some();
    let drawable_exists = state.resources.window(request.drawable).is_some()
        || state.resources.pixmap(request.drawable).is_some();
    let draw_state = state.resources.resolve_draw_state(request.gc);
    let target = state.resources.host_drawable_target(request.drawable);
    if !gc_exists {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_GC,
            request.gc.0,
            72,
        );
    }
    if !drawable_exists {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_DRAWABLE,
            request.drawable.0,
            72,
        );
    }
    if request.format != x11::ImageFormat::ZPixmap {
        return Ok(RequestOutcome::Handled);
    }
    if request.left_pad != 0 {
        return Ok(RequestOutcome::Handled);
    }
    if let Some(target) = target {
        if request.depth != target.depth() {
            return Ok(RequestOutcome::Handled);
        }
        let Some(expected_len) = zpixmap_expected_len(request.width, request.height, request.depth)
        else {
            return Ok(RequestOutcome::Handled);
        };
        if request.data.len() < expected_len {
            return Ok(RequestOutcome::Handled);
        }
        let st = draw_state.unwrap_or_default();
        backend.apply_clip_state(origin, &st.clip)?;
        backend.apply_draw_state(origin, &st)?;
        backend.put_image(
            origin,
            target.host_xid(),
            request.depth,
            request.width,
            request.height,
            request.dst_x,
            request.dst_y,
            &request.data[..expected_len],
        )?;
        let _dropped = accumulate_damage_to_state(
            state,
            request.drawable,
            request.dst_x,
            request.dst_y,
            request.width,
            request.height,
        );
    }
    Ok(RequestOutcome::Handled)
}

/// Compute the expected ZPixmap data length for `width × height` at
/// `depth`, returning `None` for unsupported depths or arithmetic
/// overflow. Mirrors `nested::zpixmap_expected_len`.
fn zpixmap_expected_len(width: u16, height: u16, depth: u8) -> Option<usize> {
    let stride_bytes: usize = match depth {
        24 | 32 => {
            let stride_bits = usize::from(width).checked_mul(32)?;
            stride_bits.div_ceil(32).checked_mul(4)?
        }
        8 => usize::from(width).div_ceil(4).checked_mul(4)?,
        4 => usize::from(width).div_ceil(8).checked_mul(4)?,
        1 => usize::from(width).div_ceil(32).checked_mul(4)?,
        _ => return None,
    };
    stride_bytes.checked_mul(usize::from(height))
}

