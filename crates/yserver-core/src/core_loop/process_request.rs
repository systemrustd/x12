fn mirror_shape_to_host_state(
    state: &ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    window: ResourceId,
    kind: u8,
) {
    use yserver_protocol::x11::shape as x11shape;
    if kind != x11shape::KIND_BOUNDING && kind != x11shape::KIND_CLIP {
        return;
    }
    let Some(w) = state.resources.window(window) else {
        return;
    };
    let Some(host_xid) = w.host_xid else {
        return;
    };
    let rects = crate::nested::shape_rects_for(state, window, kind);
    let _ = backend.set_shape_rectangles(origin, host_xid.as_raw(), kind, &rects);
}

fn drawable_full_rect_xfixes(
    state: &ServerState,
    drawable: ResourceId,
) -> yserver_protocol::x11::xfixes::RegionRect {
    use yserver_protocol::x11::xfixes;
    if let Some(window) = state.resources.window(drawable) {
        return xfixes::RegionRect {
            x: 0,
            y: 0,
            width: window.width,
            height: window.height,
        };
    }
    state.resources.pixmap(drawable).map_or(
        xfixes::RegionRect {
            x: 0,
            y: 0,
            width: 0,
            height: 0,
        },
        |pixmap| xfixes::RegionRect {
            x: 0,
            y: 0,
            width: pixmap.width,
            height: pixmap.height,
        },
    )
}

fn normalize_region_rects(
    mut rects: Vec<yserver_protocol::x11::xfixes::RegionRect>,
) -> Vec<yserver_protocol::x11::xfixes::RegionRect> {
    const MAX_RECTS: usize = 4096;
    rects.retain(|rect| !rect.is_empty());
    rects.truncate(MAX_RECTS);
    rects
}

fn resolve_host_subwindow_visual_to_state(
    state: &ServerState,
    window: ResourceId,
) -> crate::host_x11::HostSubwindowVisual {
    use crate::host_x11::HostSubwindowVisual;
    let Some(window) = state.resources.window(window) else {
        return HostSubwindowVisual::CopyFromParent;
    };
    if window.visual == crate::resources::ROOT_VISUAL {
        return HostSubwindowVisual::CopyFromParent;
    }
    let Some(visual) = state.resources.visual(window.visual) else {
        return HostSubwindowVisual::CopyFromParent;
    };
    let Some(visual_xid) = visual.host_visual_xid else {
        return HostSubwindowVisual::CopyFromParent;
    };
    let Some(colormap) = state.resources.colormap_for_visual(window.visual) else {
        return HostSubwindowVisual::CopyFromParent;
    };
    let Some(colormap_xid) = colormap.host_colormap_xid else {
        return HostSubwindowVisual::CopyFromParent;
    };
    HostSubwindowVisual::Explicit {
        depth: visual.depth,
        visual_xid: visual_xid.as_raw(),
        colormap_xid: colormap_xid.as_raw(),
    }
}

/// Drop every COMPOSITE NameWindowPixmap alias on `window` (state +
/// host side). Mirrors `nested::invalidate_composite_named_pixmaps`.
fn invalidate_composite_named_pixmaps_to_state(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    window: ResourceId,
) {
    let aliases: Vec<crate::resources::NamedCompositePixmap> = state
        .resources
        .window_mut(window)
        .map(|w| std::mem::take(&mut w.composite_named_pixmaps))
        .unwrap_or_default();
    if aliases.is_empty() {
        return;
    }
    for a in &aliases {
        let _ = state.resources.free_pixmap(a.client_pixmap);
    }
    for a in &aliases {
        let _ = backend.free_pixmap(origin, a.host_pixmap.as_raw());
    }
}

/// One window's worth of identity captured *before* destroy_window
/// runs, so the subsequent UnmapNotify+DestroyNotify fanout has stable
/// targets even though the resource table has already let go of the
/// Window struct.
struct PendingDestroy {
    window: ResourceId,
    parent: ResourceId,
    was_mapped: bool,
    host_xid: Option<crate::backend::WindowHandle>,
    on_window: Vec<ClientId>,
    on_parent: Vec<ClientId>,
}

fn collect_destroy_order(
    table: &crate::resources::ResourceTable,
    root: ResourceId,
    out: &mut Vec<ResourceId>,
) {
    let Some(w) = table.window(root) else {
        return;
    };
    for child in w.children.clone() {
        collect_destroy_order(table, child, out);
    }
    out.push(root);
}

fn fanout_destroy_sequence_to_state(state: &mut ServerState, pending: &PendingDestroy) {
    let window = pending.window;
    let parent = pending.parent;
    if pending.was_mapped {
        let _dropped = fanout_event_to_clients(state, &pending.on_window, |buf, seq, order| {
            x11::encode_unmap_notify_event(buf, seq, order, window, window, false);
        });
        let _dropped = fanout_event_to_clients(state, &pending.on_parent, |buf, seq, order| {
            x11::encode_unmap_notify_event(buf, seq, order, parent, window, false);
        });
    }
    let _dropped = fanout_event_to_clients(state, &pending.on_window, |buf, seq, order| {
        x11::encode_destroy_notify_event(buf, seq, order, window, window);
    });
    let _dropped = fanout_event_to_clients(state, &pending.on_parent, |buf, seq, order| {
        x11::encode_destroy_notify_event(buf, seq, order, parent, window);
    });
}

/// Common subtree-destroy used by both DestroyWindow (root = the
/// requested window) and DestroySubwindows (each child of the
/// requested parent).
fn destroy_window_subtree(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    root: ResourceId,
) {
    let mut order: Vec<ResourceId> = Vec::new();
    collect_destroy_order(&state.resources, root, &mut order);
    let mut pending: Vec<PendingDestroy> = Vec::new();
    for w in &order {
        let (parent, was_mapped, host_xid) =
            state
                .resources
                .window(*w)
                .map_or((ROOT_WINDOW, false, None), |win| {
                    (
                        win.parent,
                        win.map_state != MapState::Unmapped,
                        win.host_xid,
                    )
                });
        let on_window = subscribers_by_id(state, *w, 0x0002_0000);
        let on_parent = subscribers_by_id(state, parent, 0x0008_0000);
        pending.push(PendingDestroy {
            window: *w,
            parent,
            was_mapped,
            host_xid,
            on_window,
            on_parent,
        });
    }
    let bg_pixmap_xids = state.resources.collect_bg_pixmap_host_xids(root);
    let _ = state.resources.destroy_window(root);
    state.drop_window_subscriptions(&order);

    for xid in &bg_pixmap_xids {
        let _ = backend.free_pixmap(origin, *xid);
    }
    for entry in pending {
        if let Some(xid) = entry.host_xid {
            backend.unregister_host_window(xid.as_raw());
            let _ = backend.update_host_event_mask(
                origin,
                xid.as_raw(),
                crate::host_x11::POINTER_EVENT_MASK,
                false,
            );
            let _ = backend.update_host_event_mask(
                origin,
                xid.as_raw(),
                crate::host_x11::SUBWINDOW_EVENT_MASK,
                false,
            );
            let _ = backend.destroy_subwindow(origin, xid.as_raw());
        }
        fanout_destroy_sequence_to_state(state, &entry);
    }
}

#[allow(clippy::too_many_arguments, clippy::too_many_lines)]
fn handle_render_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use crate::{
        nested::{ChangePictureAttr, change_picture_translate_xids},
        resources::{ARGB_VISUAL, GlyphSetState, PictureState},
    };
    use yserver_protocol::x11::ClientByteOrder;
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    match minor {
        0 => {
            let (major, minor_ver) = backend.render_query_version(origin).unwrap_or((0, 11));
            let mut buf: Vec<u8> = Vec::with_capacity(32);
            x11::write_render_query_version_reply(
                &mut buf, byte_order, sequence, major, minor_ver,
            )?;
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        1 => {
            let mut buf: Vec<u8> = Vec::with_capacity(256);
            x11::write_render_query_pict_formats_reply(
                &mut buf,
                byte_order,
                sequence,
                crate::resources::ROOT_VISUAL,
                ARGB_VISUAL,
            )?;
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        2 => {
            let mut buf: Vec<u8> = Vec::with_capacity(32);
            x11::write_render_query_pict_index_values_reply(&mut buf, byte_order, sequence)?;
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        4 => {
            let Some(req) = x11::render_create_picture_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let host_drawable_handle = state
                .resources
                .host_drawable_target(req.drawable)
                .map(|t| t.host_handle());
            let host_pic = host_drawable_handle.and_then(|host_drawable| {
                backend
                    .render_create_picture(
                        origin,
                        host_drawable,
                        req.format,
                        req.value_mask,
                        &req.values,
                    )
                    .ok()
                    .flatten()
            });
            if let Some(host_pic) = host_pic {
                state.resources.create_picture(
                    req.picture,
                    PictureState {
                        client: client_id,
                        host_picture_xid: host_pic,
                        host_owned_pixmap: None,
                        kind: crate::resources::PictureKind::Drawable,
                    },
                );
            }
        }
        5 => {
            if body.len() < 8 {
                return Ok(RequestOutcome::Handled);
            }
            let pic_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let value_mask = u32::from_le_bytes([body[4], body[5], body[6], body[7]]);
            let translated = change_picture_translate_xids(value_mask, &body[8..], |attr, xid| {
                let resource = ResourceId(xid);
                match attr {
                    ChangePictureAttr::ClipMask => state
                        .resources
                        .pixmap(resource)
                        .and_then(|p| p.host_xid)
                        .map(|h| h.as_raw()),
                    ChangePictureAttr::AlphaMap => state
                        .resources
                        .picture(resource)
                        .map(|p| p.host_picture_xid.as_raw()),
                }
            });
            let Some(translated_values) = translated else {
                return Ok(RequestOutcome::Handled);
            };
            let mut patched = Vec::with_capacity(8 + translated_values.len());
            patched.extend_from_slice(&body[..8]);
            patched.extend_from_slice(&translated_values);
            let host_pic = state
                .resources
                .picture(pic_id)
                .map(|p| p.host_picture_xid.as_raw());
            if let Some(hp) = host_pic {
                let _ = backend.render_change_picture(origin, hp, &patched);
            }
        }
        6 => {
            if body.len() < 8 {
                return Ok(RequestOutcome::Handled);
            }
            let pic_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let host_pic = state
                .resources
                .picture(pic_id)
                .map(|p| p.host_picture_xid.as_raw());
            if let Some(hp) = host_pic {
                let _ = backend.render_set_picture_clip_rectangles(origin, hp, body);
            }
        }
        7 => {
            let Some(pic_id) = x11::render_free_resource_id(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let st = state.resources.free_picture(pic_id);
            if let Some(st) = st {
                let _ = backend.render_free_picture(origin, st.host_picture_xid.as_raw());
                if let Some(pix) = st.host_owned_pixmap {
                    let _ = backend.free_pixmap(origin, pix.as_raw());
                }
            }
        }
        8 => {
            let Some(req) = x11::render_composite_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            if dst_picture_is_sourceless(state, req.dst) {
                return emit_x11_error_with_minor(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_DRAWABLE,
                    req.dst.0,
                    u16::from(minor),
                    header.opcode,
                );
            }
            let host_src = state
                .resources
                .picture(req.src)
                .map(|p| p.host_picture_xid.as_raw());
            let host_mask = if req.mask.0 == 0 {
                Some(0)
            } else {
                state
                    .resources
                    .picture(req.mask)
                    .map(|p| p.host_picture_xid.as_raw())
            };
            let host_dst = state
                .resources
                .picture(req.dst)
                .map(|p| p.host_picture_xid.as_raw());
            if let (Some(host_src), Some(host_mask), Some(host_dst)) =
                (host_src, host_mask, host_dst)
            {
                let _ = backend.render_composite(
                    origin, req.op, host_src, host_mask, host_dst, req.src_x, req.src_y,
                    req.mask_x, req.mask_y, req.dst_x, req.dst_y, req.width, req.height,
                );
            }
        }
        10..=13 => {
            // Trapezoids (10), Triangles (11), TriStrip (12), TriFan (13).
            // All four share the same fixed prefix; the variable body
            // layout differs and is handled by the backend.
            if body.len() < 20 {
                return Ok(RequestOutcome::Handled);
            }
            let op = body[0];
            let src = ResourceId(u32::from_le_bytes([body[4], body[5], body[6], body[7]]));
            let dst = ResourceId(u32::from_le_bytes([body[8], body[9], body[10], body[11]]));
            let ynest_mask_format = u32::from_le_bytes([body[12], body[13], body[14], body[15]]);
            let src_x = i16::from_le_bytes([body[16], body[17]]);
            let src_y = i16::from_le_bytes([body[18], body[19]]);
            let primitives = &body[20..];
            if dst_picture_is_sourceless(state, dst) {
                return emit_x11_error_with_minor(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_DRAWABLE,
                    dst.0,
                    u16::from(minor),
                    header.opcode,
                );
            }
            let host_src = state
                .resources
                .picture(src)
                .map(|p| p.host_picture_xid.as_raw());
            let host_dst = state
                .resources
                .picture(dst)
                .map(|p| p.host_picture_xid.as_raw());
            let host_mask_format = if ynest_mask_format == 0 {
                Some(0u32)
            } else {
                backend.render_format_for_ynest_id(ynest_mask_format)
            };
            if let (Some(host_src), Some(host_dst), Some(host_mask_fmt)) =
                (host_src, host_dst, host_mask_format)
            {
                if minor == 10 {
                    let _ = backend.render_trapezoids(
                        origin,
                        op,
                        host_src,
                        host_dst,
                        host_mask_fmt,
                        src_x,
                        src_y,
                        primitives,
                        0,
                        0,
                    );
                } else {
                    let _ = backend.render_triangles_op(
                        origin,
                        minor,
                        op,
                        host_src,
                        host_dst,
                        host_mask_fmt,
                        src_x,
                        src_y,
                        primitives,
                        0,
                        0,
                    );
                }
            }
        }
        17 => {
            let Some((gs_id, fmt)) = x11::render_create_glyphset_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let host_gs = backend.render_create_glyphset(origin, fmt).ok().flatten();
            if let Some(host_gs) = host_gs {
                state.resources.create_glyphset(
                    gs_id,
                    GlyphSetState {
                        client: client_id,
                        host_glyphset_xid: host_gs,
                    },
                );
            }
        }
        18 => {
            let Some((new_glyphset, existing)) = x11::render_reference_glyphset_request(body)
            else {
                return Ok(RequestOutcome::Handled);
            };
            let _ = state
                .resources
                .reference_glyphset(client_id, new_glyphset, existing);
        }
        19 => {
            let Some(gs_id) = x11::render_free_resource_id(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let st = state.resources.free_glyphset(gs_id);
            if let Some(st) = st {
                let _ = backend.render_free_glyphset(origin, st.host_glyphset_xid.as_raw());
            }
        }
        20 => {
            let Some((gs_id, tail)) = x11::render_add_glyphs_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let host_gs = state
                .resources
                .glyphset(gs_id)
                .map(|g| g.host_glyphset_xid.as_raw());
            if let Some(host_gs) = host_gs {
                let _ = backend.render_add_glyphs(origin, host_gs, &tail);
            }
        }
        22 => {
            let Some((gs_id, glyph_ids)) = x11::render_free_glyphs_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let host_gs = state
                .resources
                .glyphset(gs_id)
                .map(|g| g.host_glyphset_xid.as_raw());
            if let Some(host_gs) = host_gs {
                let _ = backend.render_free_glyphs(origin, host_gs, &glyph_ids);
            }
        }
        23..=25 => {
            let Some(req) = x11::render_composite_glyphs_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            if dst_picture_is_sourceless(state, req.dst) {
                return emit_x11_error_with_minor(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_DRAWABLE,
                    req.dst.0,
                    u16::from(minor),
                    header.opcode,
                );
            }
            let host_src = state
                .resources
                .picture(req.src)
                .map(|p| p.host_picture_xid.as_raw());
            let host_dst = state
                .resources
                .picture(req.dst)
                .map(|p| p.host_picture_xid.as_raw());
            let host_gs = state
                .resources
                .glyphset(req.glyphset)
                .map(|g| g.host_glyphset_xid.as_raw());
            if let (Some(host_src), Some(host_dst), Some(host_gs)) = (host_src, host_dst, host_gs) {
                let mask_fmt = if req.mask_format == 0 {
                    0
                } else {
                    backend
                        .render_format_for_ynest_id(req.mask_format)
                        .unwrap_or(0)
                };
                let _ = backend.render_composite_glyphs(
                    origin, minor, req.op, host_src, host_dst, mask_fmt, host_gs, req.src_x,
                    req.src_y, &req.items, 0, 0,
                );
            }
        }
        26 => {
            let Some(req) = x11::render_fill_rectangles_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            if dst_picture_is_sourceless(state, req.dst) {
                return emit_x11_error_with_minor(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_DRAWABLE,
                    req.dst.0,
                    u16::from(minor),
                    header.opcode,
                );
            }
            let host_dst = state
                .resources
                .picture(req.dst)
                .map(|p| p.host_picture_xid.as_raw());
            if let Some(host_dst) = host_dst {
                let _ = backend
                    .render_fill_rectangles(origin, host_dst, req.op, req.color, &req.rects, 0, 0);
            }
        }
        27 => {
            if body.len() < 12 {
                return Ok(RequestOutcome::Handled);
            }
            let cursor_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let src_pic_id = ResourceId(u32::from_le_bytes([body[4], body[5], body[6], body[7]]));
            let x = u16::from_le_bytes([body[8], body[9]]);
            let y = u16::from_le_bytes([body[10], body[11]]);
            let host_src = state
                .resources
                .picture(src_pic_id)
                .map(|p| p.host_picture_xid);
            if let Some(host_src) = host_src
                && let Some(cursor_handle) = backend
                    .render_create_cursor(origin, host_src, x, y)
                    .ok()
                    .flatten()
            {
                state.resources.create_glyph_cursor(client_id, cursor_id);
                state
                    .resources
                    .set_cursor_host_xid(cursor_id, cursor_handle);
            }
        }
        28 => {
            if body.len() < 40 {
                return Ok(RequestOutcome::Handled);
            }
            let pic_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let host_pic = state
                .resources
                .picture(pic_id)
                .map(|p| p.host_picture_xid.as_raw());
            if let Some(hp) = host_pic {
                let _ = backend.render_set_picture_transform(origin, hp, body);
            }
        }
        29 => {
            let mut buf: Vec<u8> = Vec::with_capacity(64);
            x11::write_render_query_filters_reply(&mut buf, byte_order, sequence)?;
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        30 => {
            if body.len() < 8 {
                return Ok(RequestOutcome::Handled);
            }
            let pic_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let host_pic = state
                .resources
                .picture(pic_id)
                .map(|p| p.host_picture_xid.as_raw());
            if let Some(hp) = host_pic {
                let _ = backend.render_set_picture_filter(origin, hp, body);
            }
        }
        33 => {
            let Some((pic_id, color)) = x11::render_create_solid_fill_request(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let host_pic = backend
                .render_create_solid_fill(origin, color)
                .ok()
                .flatten();
            if let Some(host_pic) = host_pic {
                state.resources.create_picture(
                    pic_id,
                    PictureState {
                        client: client_id,
                        host_picture_xid: host_pic,
                        host_owned_pixmap: None,
                        kind: crate::resources::PictureKind::Sourceless,
                    },
                );
            }
        }
        34 => {
            if body.len() < 24 {
                return Ok(RequestOutcome::Handled);
            }
            let pic_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let host_pic = backend
                .render_create_linear_gradient(origin, body)
                .ok()
                .flatten();
            if let Some(host_pic) = host_pic {
                state.resources.create_picture(
                    pic_id,
                    PictureState {
                        client: client_id,
                        host_picture_xid: host_pic,
                        host_owned_pixmap: None,
                        kind: crate::resources::PictureKind::Sourceless,
                    },
                );
            }
        }
        35 => {
            if body.len() < 32 {
                return Ok(RequestOutcome::Handled);
            }
            let pic_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let host_pic = backend
                .render_create_radial_gradient(origin, body)
                .ok()
                .flatten();
            if let Some(host_pic) = host_pic {
                state.resources.create_picture(
                    pic_id,
                    PictureState {
                        client: client_id,
                        host_picture_xid: host_pic,
                        host_owned_pixmap: None,
                        kind: crate::resources::PictureKind::Sourceless,
                    },
                );
            }
        }
        31 | 32 | 36 => {
            // CreateAnimCursor / AddTraps / CreateConicalGradient — stubs.
        }
        _ => {
            debug!(
                "client {} #{} RENDER::unknown minor={}",
                client_id.0, sequence.0, minor
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

fn handle_randr_request(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::{ClientByteOrder, randr as x11randr};
    const RANDR_MAJOR_OPCODE: u8 = 128;
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    match minor {
        x11randr::RR_QUERY_VERSION => {
            let (reply_major, reply_minor) = x11randr::parse_query_version(body)
                .map(|r| {
                    let reply_major = x11randr::MAJOR_VERSION;
                    let reply_minor = if r.major < x11randr::MAJOR_VERSION {
                        r.minor
                    } else {
                        x11randr::MINOR_VERSION
                    };
                    (reply_major, reply_minor)
                })
                .unwrap_or((x11randr::MAJOR_VERSION, x11randr::MINOR_VERSION));
            let buf = x11randr::encode_query_version_reply(
                byte_order,
                sequence,
                reply_major,
                reply_minor,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_SCREEN_SIZE_RANGE => {
            let (min_w, min_h, max_w, max_h) = state.randr.screen_size_range();
            let buf = x11randr::encode_get_screen_size_range_reply(
                byte_order, sequence, min_w, min_h, max_w, max_h,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_SCREEN_RESOURCES | x11randr::RR_GET_SCREEN_RESOURCES_CURRENT => {
            let resources = state.randr.screen_resources_current();
            let buf = x11randr::encode_get_screen_resources_current_reply(
                byte_order, sequence, &resources,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_OUTPUT_INFO => {
            let Some(req) = x11randr::parse_output_request(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_VALUE,
                    0,
                    RANDR_MAJOR_OPCODE,
                );
            };
            let Some(info_data) = state.randr.output_info(req.output, req.config_timestamp) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_VALUE,
                    req.output,
                    RANDR_MAJOR_OPCODE,
                );
            };
            let crtc_ids = [crate::randr::CRTC_ID];
            let mode_ids = [crate::randr::MODE_ID];
            let buf = x11randr::encode_get_output_info_reply(
                byte_order,
                sequence,
                &x11randr::OutputInfoReply {
                    timestamp: info_data.timestamp,
                    crtc: info_data.crtc,
                    width_mm: info_data.width_mm,
                    height_mm: info_data.height_mm,
                    connection: 0,
                    subpixel_order: 0,
                    crtcs: &crtc_ids,
                    modes: &mode_ids,
                    clones: &[],
                    name: b"ynest-0",
                },
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_CRTC_INFO => {
            let Some(req) = x11randr::parse_crtc_request(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_VALUE,
                    0,
                    RANDR_MAJOR_OPCODE,
                );
            };
            let Some(crtc_data) = state.randr.crtc_info(req.crtc, req.config_timestamp) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_VALUE,
                    req.crtc,
                    RANDR_MAJOR_OPCODE,
                );
            };
            let output_ids = [crate::randr::OUTPUT_ID];
            let buf = x11randr::encode_get_crtc_info_reply(
                byte_order,
                sequence,
                &x11randr::CrtcInfoReply {
                    timestamp: crtc_data.timestamp,
                    x: 0,
                    y: 0,
                    width: crtc_data.width,
                    height: crtc_data.height,
                    mode: crate::randr::MODE_ID,
                    rotation: 1,
                    rotations: 1,
                    outputs: &output_ids,
                    possible: &output_ids,
                },
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_CRTC_TRANSFORM => {
            let buf = x11randr::encode_get_crtc_transform_reply(byte_order, sequence);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_LIST_OUTPUT_PROPERTIES => {
            let buf = x11randr::encode_list_output_properties_reply(byte_order, sequence);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_PANNING => {
            let timestamp = state.randr.timestamp;
            let buf = x11randr::encode_get_panning_reply(byte_order, sequence, timestamp);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_OUTPUT_PRIMARY => {
            let buf = x11randr::encode_get_output_primary_reply(byte_order, sequence, 0);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_PROVIDERS => {
            let timestamp = state.randr.timestamp;
            let buf = x11randr::encode_get_providers_reply(byte_order, sequence, timestamp);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_MONITORS => {
            let t = state.randr.timestamp;
            let w = state.randr.screen_width;
            let h = state.randr.screen_height;
            let wmm = state.randr.width_mm;
            let hmm = state.randr.height_mm;
            let name_atom = state.atoms.intern("ynest-0", false).0;
            let output_ids = [crate::randr::OUTPUT_ID];
            let buf = x11randr::encode_get_monitors_reply(
                byte_order,
                sequence,
                t,
                &[x11randr::MonitorInfo {
                    name: name_atom,
                    primary: true,
                    x: 0,
                    y: 0,
                    width: w,
                    height: h,
                    width_mm: wmm,
                    height_mm: hmm,
                    outputs: &output_ids,
                }],
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_CRTC_GAMMA_SIZE => {
            let buf = x11randr::encode_get_crtc_gamma_size_reply(byte_order, sequence, 0);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_CRTC_GAMMA => {
            let buf = x11randr::encode_get_crtc_gamma_reply(byte_order, sequence, 0);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_GET_OUTPUT_PROPERTY => {
            let buf = x11randr::encode_get_output_property_reply(byte_order, sequence);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_SELECT_INPUT => {
            if let Some(req) = x11randr::parse_select_input(body) {
                if req.enable == 0 {
                    state
                        .randr_select_masks
                        .remove(&(client_id.0, ResourceId(req.window)));
                } else {
                    state
                        .randr_select_masks
                        .insert((client_id.0, ResourceId(req.window)), req.enable);
                }
            }
        }
        x11randr::RR_GET_SCREEN_INFO => {
            let timestamp = state.randr.timestamp;
            let config_timestamp = state.randr.config_timestamp;
            let width = state.randr.screen_width;
            let height = state.randr.screen_height;
            let mwidth = u16::try_from(state.randr.width_mm).unwrap_or(u16::MAX);
            let mheight = u16::try_from(state.randr.height_mm).unwrap_or(u16::MAX);
            let buf = x11randr::encode_get_screen_info_reply(
                byte_order,
                sequence,
                ROOT_WINDOW.0,
                timestamp,
                config_timestamp,
                width,
                height,
                mwidth,
                mheight,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &buf));
        }
        x11randr::RR_SET_SCREEN_CONFIG | x11randr::RR_SET_CRTC_CONFIG => {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                0,
                RANDR_MAJOR_OPCODE,
            );
        }
        other => {
            debug!(
                "client {} #{} RANDR::unknown minor={}",
                client_id.0, sequence.0, other
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

fn handle_sync_request(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::{ClientByteOrder, sync as x11sync};
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    match minor {
        x11sync::INITIALIZE => {
            let (client_major, client_minor) =
                x11sync::parse_initialize(body).unwrap_or((x11sync::MAJOR_VERSION, 0));
            let major = x11sync::MAJOR_VERSION.min(client_major);
            let minor_ver = if major < x11sync::MAJOR_VERSION {
                client_minor
            } else {
                x11sync::MINOR_VERSION
            };
            let reply = x11sync::encode_initialize_reply(byte_order, sequence, major, minor_ver);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11sync::LIST_SYSTEM_COUNTERS => {
            let reply = x11sync::encode_list_system_counters_empty_reply(byte_order, sequence);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11sync::CREATE_COUNTER => {
            if let Some((counter, value)) = x11sync::parse_counter_value(body) {
                state.sync_counters.insert(
                    counter,
                    crate::server::SyncCounter {
                        owner: client_id,
                        value,
                    },
                );
            }
        }
        x11sync::SET_COUNTER => {
            if let Some((counter, value)) = x11sync::parse_counter_value(body)
                && let Some(c) = state.sync_counters.get_mut(&counter)
            {
                c.value = value;
            }
        }
        x11sync::CHANGE_COUNTER => {
            if let Some((counter, delta)) = x11sync::parse_counter_value(body)
                && let Some(c) = state.sync_counters.get_mut(&counter)
            {
                c.value = c.value.saturating_add(delta);
            }
        }
        x11sync::QUERY_COUNTER => {
            let counter = x11sync::parse_resource(body).unwrap_or(0);
            let value = state.sync_counters.get(&counter).map_or(0, |c| c.value);
            let reply = x11sync::encode_query_counter_reply(byte_order, sequence, value);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11sync::DESTROY_COUNTER => {
            if let Some(counter) = x11sync::parse_resource(body) {
                state.sync_counters.remove(&counter);
            }
        }
        x11sync::AWAIT => {
            // Non-blocking stub.
        }
        x11sync::CREATE_ALARM => {
            if let Some((alarm, _mask)) = x11sync::parse_alarm_with_mask(body) {
                state.sync_alarms.insert(
                    alarm,
                    crate::server::SyncAlarm {
                        owner: client_id,
                        ..crate::server::SyncAlarm::default()
                    },
                );
            }
        }
        x11sync::CHANGE_ALARM => {
            if let Some((alarm, _mask)) = x11sync::parse_alarm_with_mask(body)
                && let Some(a) = state.sync_alarms.get_mut(&alarm)
            {
                a.state = 0;
            }
        }
        x11sync::QUERY_ALARM => {
            let alarm_id = x11sync::parse_resource(body).unwrap_or(0);
            let alarm = state
                .sync_alarms
                .get(&alarm_id)
                .cloned()
                .unwrap_or_default();
            let reply = x11sync::encode_query_alarm_reply(
                byte_order,
                sequence,
                alarm.counter,
                alarm.wait_value,
                alarm.delta,
                alarm.events,
                alarm.state,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11sync::DESTROY_ALARM => {
            if let Some(alarm) = x11sync::parse_resource(body) {
                state.sync_alarms.remove(&alarm);
            }
        }
        x11sync::SET_PRIORITY => {
            // Stub.
        }
        x11sync::GET_PRIORITY => {
            let reply = x11sync::encode_get_priority_reply(byte_order, sequence, 0);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        other => {
            debug!(
                "client {} #{} SYNC::unknown minor={}",
                client_id.0, sequence.0, other
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

#[allow(clippy::too_many_arguments)]
fn handle_shape_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::{ClientByteOrder, shape as x11shape};
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    match minor {
        x11shape::QUERY_VERSION => {
            let reply = x11shape::encode_query_version_reply(byte_order, sequence);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11shape::RECTANGLES => {
            if let Some((req, rects)) = x11shape::parse_rectangles_request(body) {
                let window = ResourceId(req.dest);
                let source = crate::nested::offset_rects(rects, req.x_off, req.y_off);
                let current = crate::nested::shape_rects_for(state, window, req.dest_kind);
                let new_rects = crate::nested::apply_shape_op(current, source, req.op);
                crate::nested::set_shape_rects(state, window, req.dest_kind, new_rects);
                mirror_shape_to_host_state(state, backend, origin, window, req.dest_kind);
            }
        }
        x11shape::MASK => {
            if let Some(req) = x11shape::parse_mask_request(body) {
                let window = ResourceId(req.dest);
                if req.src == 0 {
                    crate::nested::clear_shape_rects(state, window, req.dest_kind);
                    mirror_shape_to_host_state(state, backend, origin, window, req.dest_kind);
                    return Ok(RequestOutcome::Handled);
                }
                let source = crate::nested::shape_mask_source_rects(state, ResourceId(req.src));
                let source = crate::nested::offset_rects(source, req.x_off, req.y_off);
                let current = crate::nested::shape_rects_for(state, window, req.dest_kind);
                let new_rects = crate::nested::apply_shape_op(current, source, req.op);
                crate::nested::set_shape_rects(state, window, req.dest_kind, new_rects);
                mirror_shape_to_host_state(state, backend, origin, window, req.dest_kind);
            }
        }
        x11shape::COMBINE => {
            if let Some(req) = x11shape::parse_combine_request(body) {
                let dest = ResourceId(req.dest);
                let src = ResourceId(req.src);
                let source = crate::nested::offset_rects(
                    crate::nested::shape_rects_for(state, src, req.src_kind),
                    req.x_off,
                    req.y_off,
                );
                let current = crate::nested::shape_rects_for(state, dest, req.dest_kind);
                let new_rects = crate::nested::apply_shape_op(current, source, req.op);
                crate::nested::set_shape_rects(state, dest, req.dest_kind, new_rects);
                mirror_shape_to_host_state(state, backend, origin, dest, req.dest_kind);
            }
        }
        x11shape::OFFSET => {
            if let Some(req) = x11shape::parse_offset_request(body) {
                let dest = ResourceId(req.dest);
                let mut translated = false;
                if let Some(s) = state.shape_windows.get_mut(&dest)
                    && let Some(slot) = s.rects_mut(req.dest_kind)
                    && let Some(rects) = slot.as_mut()
                {
                    crate::nested::translate_region(rects, req.x_off, req.y_off);
                    translated = true;
                }
                if translated {
                    mirror_shape_to_host_state(state, backend, origin, dest, req.dest_kind);
                }
            }
        }
        x11shape::QUERY_EXTENTS => {
            let window = ResourceId(x11shape::parse_window(body).unwrap_or(ROOT_WINDOW.0));
            let bounding_rects =
                crate::nested::shape_rects_for(state, window, x11shape::KIND_BOUNDING);
            let clip_rects = crate::nested::shape_rects_for(state, window, x11shape::KIND_CLIP);
            let bounding_shaped =
                crate::nested::shape_kind_is_set(state, window, x11shape::KIND_BOUNDING);
            let clip_shaped = crate::nested::shape_kind_is_set(state, window, x11shape::KIND_CLIP);
            let bounding = crate::nested::region_extents(&bounding_rects);
            let clip = crate::nested::region_extents(&clip_rects);
            let reply = x11shape::encode_query_extents_reply(
                byte_order,
                sequence,
                bounding_shaped,
                clip_shaped,
                bounding,
                clip,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11shape::SELECT_INPUT => {
            if let Some(req) = x11shape::parse_select_input_request(body) {
                let key = (client_id.0, ResourceId(req.window));
                if req.enable {
                    state.shape_select_masks.insert(key, true);
                } else {
                    state.shape_select_masks.remove(&key);
                }
            }
        }
        x11shape::INPUT_SELECTED => {
            let window = ResourceId(x11shape::parse_window(body).unwrap_or(ROOT_WINDOW.0));
            let enabled = state
                .shape_select_masks
                .get(&(client_id.0, window))
                .copied()
                .unwrap_or(false);
            let reply = x11shape::encode_input_selected_reply(byte_order, sequence, enabled);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11shape::GET_RECTANGLES => {
            let (window, kind) = x11shape::parse_get_rectangles_request(body)
                .map(|(w, k)| (ResourceId(w), k))
                .unwrap_or((ROOT_WINDOW, x11shape::KIND_BOUNDING));
            let rects = crate::nested::shape_rects_for(state, window, kind);
            let reply = x11shape::encode_get_rectangles_reply(
                byte_order,
                sequence,
                x11shape::ORDERING_YX_BANDED,
                &rects,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        other => {
            debug!(
                "client {} #{} SHAPE::unknown minor={}",
                client_id.0, sequence.0, other
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

#[allow(clippy::too_many_arguments)]
fn handle_xfixes_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::{ClientByteOrder, xfixes as x11xfixes};
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    match minor {
        x11xfixes::QUERY_VERSION => {
            let reply = x11xfixes::encode_query_version_reply(
                byte_order,
                sequence,
                x11xfixes::MAJOR_VERSION,
                x11xfixes::MINOR_VERSION,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11xfixes::SELECT_SELECTION_INPUT => {
            if let Some(req) = x11xfixes::parse_select_selection_input(body) {
                let key = (
                    client_id.0,
                    ResourceId(req.window),
                    yserver_protocol::x11::AtomId(req.selection),
                );
                if req.event_mask == 0 {
                    state.xfixes_selection_masks.remove(&key);
                } else {
                    state.xfixes_selection_masks.insert(key, req.event_mask);
                }
            }
        }
        x11xfixes::SELECT_CURSOR_INPUT => {
            if let Some(req) = x11xfixes::parse_select_cursor_input(body) {
                let key = (client_id.0, ResourceId(req.window));
                if req.event_mask == 0 {
                    state.xfixes_cursor_masks.remove(&key);
                } else {
                    state.xfixes_cursor_masks.insert(key, req.event_mask);
                }
            }
        }
        x11xfixes::GET_CURSOR_IMAGE => {
            let reply = x11xfixes::encode_get_cursor_image_empty_reply(byte_order, sequence);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11xfixes::CREATE_REGION => {
            if let Some((region, rects)) = x11xfixes::parse_create_region(body) {
                state.xfixes_regions.insert(
                    region,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects: normalize_region_rects(rects),
                    },
                );
            }
        }
        x11xfixes::CREATE_REGION_FROM_BITMAP | x11xfixes::CREATE_REGION_FROM_GC => {
            if let Some((region, _source)) = x11xfixes::parse_u32_pair(body) {
                state.xfixes_regions.insert(
                    region,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects: Vec::new(),
                    },
                );
            }
        }
        x11xfixes::CREATE_REGION_FROM_WINDOW => {
            if let Some((region, window)) = x11xfixes::parse_u32_pair(body) {
                let rects = state
                    .resources
                    .window(ResourceId(window))
                    .map(|w| {
                        vec![x11xfixes::RegionRect {
                            x: 0,
                            y: 0,
                            width: w.width,
                            height: w.height,
                        }]
                    })
                    .unwrap_or_default();
                state.xfixes_regions.insert(
                    region,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects,
                    },
                );
            }
        }
        x11xfixes::DESTROY_REGION => {
            if let Some((region, _)) = x11xfixes::parse_u32_pair(body) {
                state.xfixes_regions.remove(&region);
            } else if body.len() >= 4 {
                let region = u32::from_le_bytes(body[0..4].try_into().unwrap());
                state.xfixes_regions.remove(&region);
            }
        }
        x11xfixes::SET_REGION => {
            if let Some((region, rects)) = x11xfixes::parse_create_region(body) {
                state
                    .xfixes_regions
                    .entry(region)
                    .and_modify(|r| r.rects = normalize_region_rects(rects.clone()))
                    .or_insert_with(|| crate::server::XFixesRegion {
                        owner: client_id,
                        rects: normalize_region_rects(rects),
                    });
            }
        }
        x11xfixes::COPY_REGION => {
            if let Some((source, dest)) = x11xfixes::parse_u32_pair(body) {
                let rects = state
                    .xfixes_regions
                    .get(&source)
                    .map(|r| r.rects.clone())
                    .unwrap_or_default();
                state.xfixes_regions.insert(
                    dest,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects,
                    },
                );
            }
        }
        x11xfixes::UNION_REGION | x11xfixes::INTERSECT_REGION | x11xfixes::SUBTRACT_REGION => {
            if let Some((source1, source2, dest)) = x11xfixes::parse_u32_triplet(body) {
                let a = state
                    .xfixes_regions
                    .get(&source1)
                    .map(|r| r.rects.clone())
                    .unwrap_or_default();
                let b = state
                    .xfixes_regions
                    .get(&source2)
                    .map(|r| r.rects.clone())
                    .unwrap_or_default();
                let rects = match minor {
                    x11xfixes::UNION_REGION => {
                        normalize_region_rects(a.into_iter().chain(b).collect())
                    }
                    x11xfixes::INTERSECT_REGION => crate::nested::intersect_regions(&a, &b),
                    x11xfixes::SUBTRACT_REGION => {
                        if crate::nested::intersect_regions(&a, &b).is_empty() {
                            a
                        } else {
                            Vec::new()
                        }
                    }
                    _ => unreachable!(),
                };
                state.xfixes_regions.insert(
                    dest,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects,
                    },
                );
            }
        }
        x11xfixes::INVERT_REGION => {
            if let Some((_source, bounds, dest)) = x11xfixes::parse_invert_region(body) {
                state.xfixes_regions.insert(
                    dest,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects: normalize_region_rects(vec![bounds]),
                    },
                );
            }
        }
        x11xfixes::TRANSLATE_REGION => {
            if let Some((region, dx, dy)) = x11xfixes::parse_translate_region(body)
                && let Some(region) = state.xfixes_regions.get_mut(&region)
            {
                crate::nested::translate_region(&mut region.rects, dx, dy);
            }
        }
        x11xfixes::REGION_EXTENTS => {
            if let Some((source, dest)) = x11xfixes::parse_u32_pair(body) {
                let rect = state
                    .xfixes_regions
                    .get(&source)
                    .map(|r| crate::nested::region_extents(&r.rects))
                    .unwrap_or(x11xfixes::RegionRect {
                        x: 0,
                        y: 0,
                        width: 0,
                        height: 0,
                    });
                state.xfixes_regions.insert(
                    dest,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects: normalize_region_rects(vec![rect]),
                    },
                );
            }
        }
        x11xfixes::FETCH_REGION => {
            let region = body
                .get(0..4)
                .map(|bytes| u32::from_le_bytes(bytes.try_into().unwrap()))
                .unwrap_or(0);
            let rects = state
                .xfixes_regions
                .get(&region)
                .map(|r| r.rects.clone())
                .unwrap_or_default();
            let extents = crate::nested::region_extents(&rects);
            let reply = x11xfixes::encode_fetch_region_reply(byte_order, sequence, extents, &rects);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11xfixes::CHANGE_CURSOR_BY_NAME => {
            if let Some((cursor_xid, name_bytes)) = x11xfixes::parse_change_cursor_by_name(body) {
                let host_cursor = state.resources.cursor_host_xid(ResourceId(cursor_xid));
                if let Some(host_cursor) = host_cursor {
                    let _ = backend.xfixes_change_cursor_by_name(origin, host_cursor, name_bytes);
                }
            }
        }
        x11xfixes::HIDE_CURSOR | x11xfixes::SHOW_CURSOR => {
            debug!(
                "client {} #{} XFIXES::{}Cursor (stub)",
                client_id.0,
                sequence.0,
                if minor == x11xfixes::HIDE_CURSOR {
                    "Hide"
                } else {
                    "Show"
                }
            );
        }
        other => {
            debug!(
                "client {} #{} XFIXES::unknown minor={}",
                client_id.0, sequence.0, other
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

#[allow(clippy::too_many_arguments)]
fn handle_composite_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::{ClientByteOrder, composite as x11composite};
    const COMPOSITE_MAJOR_OPCODE: u8 = 144;
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    match minor {
        x11composite::QUERY_VERSION => {
            let _ = x11composite::parse_query_version(body);
            let major = x11composite::MAJOR_VERSION;
            let minor_ver = x11composite::MINOR_VERSION;
            debug!(
                "client {} #{} COMPOSITE::QueryVersion -> {}.{}",
                client_id.0, sequence.0, major, minor_ver
            );
            let reply =
                x11composite::encode_query_version_reply(byte_order, sequence, major, minor_ver);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11composite::REDIRECT_WINDOW | x11composite::REDIRECT_SUBWINDOWS => {
            if let Some((window, update)) = x11composite::parse_window_update(body) {
                let subwindows = minor == x11composite::REDIRECT_SUBWINDOWS;
                state
                    .composite_redirects
                    .insert((ResourceId(window), subwindows), update);
            }
        }
        x11composite::UNREDIRECT_WINDOW | x11composite::UNREDIRECT_SUBWINDOWS => {
            if let Some((window, _update)) = x11composite::parse_window_update(body) {
                let subwindows = minor == x11composite::UNREDIRECT_SUBWINDOWS;
                state
                    .composite_redirects
                    .remove(&(ResourceId(window), subwindows));
            }
        }
        x11composite::CREATE_REGION_FROM_BORDER_CLIP => {
            if let Some((region, window)) = x11composite::parse_u32_pair(body) {
                let rects = vec![drawable_full_rect_xfixes(state, ResourceId(window))];
                state.xfixes_regions.insert(
                    region,
                    crate::server::XFixesRegion {
                        owner: client_id,
                        rects: normalize_region_rects(rects),
                    },
                );
            }
        }
        x11composite::NAME_WINDOW_PIXMAP => {
            let Some((window_raw, pixmap_raw)) = x11composite::parse_u32_pair(body) else {
                return Ok(RequestOutcome::Handled);
            };
            let window = ResourceId(window_raw);
            let pixmap = ResourceId(pixmap_raw);
            let snapshot = state.resources.window(window).map(|w| {
                let parent_redirected = state
                    .composite_redirects
                    .keys()
                    .any(|(rwid, sub)| *sub && *rwid == w.parent);
                let self_redirected = state.composite_redirects.contains_key(&(window, false));
                (
                    w.host_xid,
                    w.width,
                    w.height,
                    w.depth,
                    parent_redirected || self_redirected,
                )
            });
            let Some((host_xid, w_width, w_height, w_depth, redirected)) = snapshot else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_WINDOW,
                    window_raw,
                    COMPOSITE_MAJOR_OPCODE,
                );
            };
            if !redirected {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_MATCH,
                    window_raw,
                    COMPOSITE_MAJOR_OPCODE,
                );
            }
            let Some(host_window_xid) = host_xid else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_ALLOC,
                    pixmap_raw,
                    COMPOSITE_MAJOR_OPCODE,
                );
            };
            if backend.composite_opcode().is_none() {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_ALLOC,
                    pixmap_raw,
                    COMPOSITE_MAJOR_OPCODE,
                );
            }
            let host_pixmap_xid = match backend.name_window_pixmap(origin, host_window_xid) {
                Ok(handle) => handle,
                Err(_) => {
                    return emit_x11_error(
                        state,
                        client_id,
                        sequence,
                        x11::error::BAD_ALLOC,
                        pixmap_raw,
                        COMPOSITE_MAJOR_OPCODE,
                    );
                }
            };
            state.resources.create_pixmap(
                client_id,
                x11::CreatePixmapRequest {
                    pixmap,
                    drawable: window,
                    width: w_width,
                    height: w_height,
                    depth: w_depth,
                },
            );
            let _ = state.resources.set_pixmap_host_xid(pixmap, host_pixmap_xid);
            if let Some(w) = state.resources.window_mut(window) {
                w.composite_named_pixmaps
                    .push(crate::resources::NamedCompositePixmap {
                        client_pixmap: pixmap,
                        host_pixmap: host_pixmap_xid,
                        width: w_width,
                        height: w_height,
                    });
            }
        }
        x11composite::GET_OVERLAY_WINDOW => {
            let _window = x11composite::parse_window(body).unwrap_or(ROOT_WINDOW.0);
            let overlay = ROOT_WINDOW.0;
            let reply =
                x11composite::encode_get_overlay_window_reply(byte_order, sequence, overlay);
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11composite::RELEASE_OVERLAY_WINDOW => {
            debug!(
                "client {} #{} COMPOSITE::ReleaseOverlayWindow",
                client_id.0, sequence.0
            );
        }
        other => {
            debug!(
                "client {} #{} COMPOSITE::unknown minor={}",
                client_id.0, sequence.0, other
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

/// Special-case writer for MIT-SHM CreateSegment: sends a normal X11
/// reply alongside an SCM_RIGHTS file descriptor. This is the one path
/// where the lifted code reaches around `client_io::write_or_buffer`
/// because the kernel only delivers the fd if it accompanies inline
/// bytes — buffering would split them apart. Once the outbound buffer
/// is non-empty we can't insert an FD-carrying frame, so we drain
/// first.
fn send_reply_with_fd(
    client: &mut crate::server::ClientState,
    bytes: &[u8],
    fd: std::os::fd::RawFd,
) -> io::Result<()> {
    // Drain whatever's pending so the SCM_RIGHTS frame lands in order.
    while !client.outbound.is_empty() {
        match client_io::drain_outbound(client)? {
            client_io::WriteOutcome::Done => break,
            client_io::WriteOutcome::WouldBlock => {
                std::thread::yield_now();
            }
            client_io::WriteOutcome::Disconnect => {
                return Err(io::Error::new(
                    io::ErrorKind::BrokenPipe,
                    "client outbound disconnect during fd send",
                ));
            }
        }
    }
    let writer_arc = client.writer.clone();
    let mut w = writer_arc
        .lock()
        .map_err(|_| io::Error::new(io::ErrorKind::BrokenPipe, "client writer mutex poisoned"))?;
    crate::unix_fd::send_with_fd(&mut w, bytes, fd)
}

#[allow(clippy::too_many_arguments)]
fn handle_mit_shm_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
    attached_fd: Option<OwnedFd>,
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::mit_shm as shm;
    const MIT_SHM_MAJOR_OPCODE: u8 = 130;
    let minor = header.data;
    debug!(
        "client {} #{} MIT-SHM dispatch minor={minor} body_len={}",
        client_id.0,
        sequence.0,
        body.len()
    );
    match minor {
        shm::QUERY_VERSION => {
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let byte_order = client.byte_order;
            let reply = shm::encode_query_version_reply(byte_order, sequence, false);
            return Ok(write_to_client(client, client_id, &reply));
        }
        shm::ATTACH => {
            let Some(req) = shm::parse_attach(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_LENGTH,
                    0,
                    MIT_SHM_MAJOR_OPCODE,
                );
            };
            match crate::server::MitShmSegment::from_shmid(client_id, req.shmid, req.read_only) {
                Ok(segment) => {
                    state.mit_shm_segments.insert(req.shmseg, segment);
                    debug!(
                        "client {} #{} MIT-SHM::Attach shmseg=0x{:x} shmid=0x{:x}",
                        client_id.0, sequence.0, req.shmseg, req.shmid
                    );
                }
                Err(_) => {
                    return emit_x11_error(
                        state,
                        client_id,
                        sequence,
                        x11::error::BAD_VALUE,
                        req.shmseg,
                        MIT_SHM_MAJOR_OPCODE,
                    );
                }
            }
        }
        shm::ATTACH_FD => {
            let Some(req) = shm::parse_attach_fd(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_LENGTH,
                    0,
                    MIT_SHM_MAJOR_OPCODE,
                );
            };
            let Some(fd) = attached_fd else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_VALUE,
                    req.shmseg,
                    MIT_SHM_MAJOR_OPCODE,
                );
            };
            // OwnedFd carries an automatic close-on-drop; from_fd takes
            // raw and assumes ownership transfer.
            let raw = std::os::fd::IntoRawFd::into_raw_fd(fd);
            match crate::server::MitShmSegment::from_fd(client_id, raw, req.read_only) {
                Ok(segment) => {
                    state.mit_shm_segments.insert(req.shmseg, segment);
                }
                Err(_) => {
                    return emit_x11_error(
                        state,
                        client_id,
                        sequence,
                        x11::error::BAD_VALUE,
                        req.shmseg,
                        MIT_SHM_MAJOR_OPCODE,
                    );
                }
            }
        }
        shm::DETACH => {
            if let Some(shmseg) = shm::parse_detach(body) {
                state.mit_shm_segments.remove(&shmseg);
            }
        }
        shm::CREATE_PIXMAP => {
            let Some(req) = shm::parse_create_pixmap(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_LENGTH,
                    0,
                    MIT_SHM_MAJOR_OPCODE,
                );
            };
            return handle_mit_shm_create_pixmap(state, backend, origin, client_id, sequence, req);
        }
        shm::PUT_IMAGE => {
            let Some(req) = shm::parse_put_image(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_LENGTH,
                    0,
                    MIT_SHM_MAJOR_OPCODE,
                );
            };
            return handle_mit_shm_put_image(state, backend, origin, client_id, sequence, req);
        }
        shm::GET_IMAGE => {
            let Some(req) = shm::parse_get_image(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_LENGTH,
                    0,
                    MIT_SHM_MAJOR_OPCODE,
                );
            };
            return handle_mit_shm_get_image(state, backend, origin, client_id, sequence, req);
        }
        shm::CREATE_SEGMENT => {
            let Some(req) = shm::parse_create_segment(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_LENGTH,
                    0,
                    MIT_SHM_MAJOR_OPCODE,
                );
            };
            return handle_mit_shm_create_segment(state, client_id, sequence, req);
        }
        _ => {}
    }
    Ok(RequestOutcome::Handled)
}

fn handle_mit_shm_create_pixmap(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    req: yserver_protocol::x11::mit_shm::CreatePixmapRequest,
) -> io::Result<RequestOutcome> {
    const MIT_SHM_MAJOR_OPCODE: u8 = 130;
    debug!(
        "client {} #{} MIT-SHM::CreatePixmap pid=0x{:x} drawable=0x{:x} {}x{} d{}",
        client_id.0, sequence.0, req.pid, req.drawable, req.width, req.height, req.depth,
    );
    let validation_failed = {
        let handle = state.clients.get(&client_id.0).expect("client registered");
        let owned = crate::server::IdAllocator::validate_owned(
            req.pid,
            handle.resource_id_base,
            handle.resource_id_mask,
        );
        let in_use = state.resources.any_resource_exists(ResourceId(req.pid));
        !owned || in_use
    };
    let drawable_exists = state.resources.window(ResourceId(req.drawable)).is_some()
        || state.resources.pixmap(ResourceId(req.drawable)).is_some();
    if validation_failed {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_ID_CHOICE,
            req.pid,
            MIT_SHM_MAJOR_OPCODE,
        );
    }
    if !drawable_exists {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_DRAWABLE,
            req.drawable,
            MIT_SHM_MAJOR_OPCODE,
        );
    }
    if !supported_pixmap_depth(req.depth) {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_VALUE,
            u32::from(req.depth),
            MIT_SHM_MAJOR_OPCODE,
        );
    }
    let Some(expected_len) = zpixmap_expected_len(req.width, req.height, req.depth) else {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_VALUE,
            req.shmseg,
            MIT_SHM_MAJOR_OPCODE,
        );
    };
    let host_xid = match backend.create_pixmap(origin, req.depth, req.width, req.height) {
        Ok(handle) => Some(handle),
        Err(err) => {
            log::warn!(
                "client {} MIT-SHM::CreatePixmap host CreatePixmap failed: {err}",
                client_id.0
            );
            None
        }
    };
    let snapshot: Vec<u8> = {
        let Some(segment) = state.mit_shm_segments.get(&req.shmseg) else {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                req.shmseg,
                MIT_SHM_MAJOR_OPCODE,
            );
        };
        let bytes = segment.as_slice();
        let start = req.offset as usize;
        let end = start.saturating_add(expected_len);
        if end > bytes.len() {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                req.offset,
                MIT_SHM_MAJOR_OPCODE,
            );
        }
        bytes[start..end].to_vec()
    };
    if let Some(host_xid) = host_xid {
        let _ = backend.clear_clip_rectangles(origin);
        if let Err(err) = backend.put_image(
            origin,
            host_xid.as_raw(),
            req.depth,
            req.width,
            req.height,
            0,
            0,
            &snapshot,
        ) {
            log::warn!(
                "client {} MIT-SHM::CreatePixmap put_image failed: {err}",
                client_id.0
            );
        }
    }
    state.resources.create_pixmap(
        client_id,
        x11::CreatePixmapRequest {
            depth: req.depth,
            pixmap: ResourceId(req.pid),
            drawable: ResourceId(req.drawable),
            width: req.width,
            height: req.height,
        },
    );
    if let Some(xid) = host_xid {
        let updated = state
            .resources
            .set_pixmap_host_xid(ResourceId(req.pid), xid);
        debug_assert!(updated, "pixmap was just inserted above");
    }
    Ok(RequestOutcome::Handled)
}

fn handle_mit_shm_put_image(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    req: yserver_protocol::x11::mit_shm::PutImageRequest,
) -> io::Result<RequestOutcome> {
    const MIT_SHM_MAJOR_OPCODE: u8 = 130;
    debug!(
        "client {} #{} MIT-SHM::PutImage drawable=0x{:x} {}x{} d{}",
        client_id.0, sequence.0, req.drawable, req.src_width, req.src_height, req.depth
    );
    let target = state
        .resources
        .host_drawable_target(ResourceId(req.drawable));
    let Some(target) = target else {
        return Ok(RequestOutcome::Handled);
    };
    let Some(stride_bytes) = zpixmap_expected_len(req.src_width, req.src_height, req.depth) else {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_VALUE,
            req.shmseg,
            MIT_SHM_MAJOR_OPCODE,
        );
    };
    let snapshot: Vec<u8> = {
        let Some(segment) = state.mit_shm_segments.get(&req.shmseg) else {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                req.shmseg,
                MIT_SHM_MAJOR_OPCODE,
            );
        };
        let bytes = segment.as_slice();
        let start = req.offset as usize;
        let end = start.saturating_add(stride_bytes);
        if end > bytes.len() {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                req.offset,
                MIT_SHM_MAJOR_OPCODE,
            );
        }
        bytes[start..end].to_vec()
    };
    backend.clear_clip_rectangles(origin)?;
    backend.put_image(
        origin,
        target.host_xid(),
        req.depth,
        req.src_width,
        req.src_height,
        req.dst_x,
        req.dst_y,
        &snapshot,
    )?;
    let _dropped = accumulate_damage_to_state(
        state,
        ResourceId(req.drawable),
        req.dst_x,
        req.dst_y,
        req.src_width,
        req.src_height,
    );
    Ok(RequestOutcome::Handled)
}

fn handle_mit_shm_get_image(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    req: yserver_protocol::x11::mit_shm::GetImageRequest,
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::mit_shm as shm;
    const MIT_SHM_MAJOR_OPCODE: u8 = 130;
    {
        let Some(segment) = state.mit_shm_segments.get(&req.shmseg) else {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                req.shmseg,
                MIT_SHM_MAJOR_OPCODE,
            );
        };
        if segment.read_only {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_ACCESS,
                req.shmseg,
                MIT_SHM_MAJOR_OPCODE,
            );
        }
    }
    let target = state
        .resources
        .host_drawable_target(ResourceId(req.drawable));
    let Some(target) = target else {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_DRAWABLE,
            req.drawable,
            MIT_SHM_MAJOR_OPCODE,
        );
    };
    let host_bytes = backend
        .get_image(
            origin,
            target.host_xid(),
            req.format,
            req.x,
            req.y,
            req.width,
            req.height,
            req.plane_mask,
        )
        .ok()
        .flatten();
    let Some(host_reply_bytes) = host_bytes else {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_DRAWABLE,
            req.drawable,
            MIT_SHM_MAJOR_OPCODE,
        );
    };
    let pixel_data: Vec<u8> = host_reply_bytes.get(32..).unwrap_or(&[]).to_vec();
    {
        let Some(segment) = state.mit_shm_segments.get_mut(&req.shmseg) else {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                req.shmseg,
                MIT_SHM_MAJOR_OPCODE,
            );
        };
        let Some(buf) = segment.as_mut_slice() else {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_ACCESS,
                req.shmseg,
                MIT_SHM_MAJOR_OPCODE,
            );
        };
        let start = req.offset as usize;
        let end = start.saturating_add(pixel_data.len());
        if end > buf.len() {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_VALUE,
                req.offset,
                MIT_SHM_MAJOR_OPCODE,
            );
        }
        buf[start..end].copy_from_slice(&pixel_data);
    }
    let depth = host_reply_bytes.first().copied().unwrap_or(24);
    let visual = crate::resources::ROOT_VISUAL.0;
    #[allow(clippy::cast_possible_truncation)]
    let size = pixel_data.len() as u32;
    let Some(client) = state.clients.get_mut(&client_id.0) else {
        return Ok(RequestOutcome::Handled);
    };
    let byte_order = client.byte_order;
    let reply = shm::encode_get_image_reply(byte_order, sequence, depth, visual, size);
    Ok(write_to_client(client, client_id, &reply))
}

fn handle_mit_shm_create_segment(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    req: yserver_protocol::x11::mit_shm::CreateSegmentRequest,
) -> io::Result<RequestOutcome> {
    const MIT_SHM_MAJOR_OPCODE: u8 = 130;
    if req.size == 0 {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_VALUE,
            req.shmseg,
            MIT_SHM_MAJOR_OPCODE,
        );
    }
    let fd = unsafe { libc::memfd_create(c"yserver-shm".as_ptr(), libc::MFD_CLOEXEC) };
    if fd < 0 {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_ALLOC,
            req.shmseg,
            MIT_SHM_MAJOR_OPCODE,
        );
    }
    if unsafe { libc::ftruncate(fd, libc::off_t::from(req.size as i32)) } < 0 {
        unsafe { libc::close(fd) };
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_ALLOC,
            req.shmseg,
            MIT_SHM_MAJOR_OPCODE,
        );
    }
    let fd_for_client = unsafe { libc::dup(fd) };
    if fd_for_client < 0 {
        unsafe { libc::close(fd) };
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_ALLOC,
            req.shmseg,
            MIT_SHM_MAJOR_OPCODE,
        );
    }
    let segment = match crate::server::MitShmSegment::from_fd(client_id, fd, req.read_only) {
        Ok(s) => s,
        Err(_) => {
            unsafe { libc::close(fd_for_client) };
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_ALLOC,
                req.shmseg,
                MIT_SHM_MAJOR_OPCODE,
            );
        }
    };
    state.mit_shm_segments.insert(req.shmseg, segment);
    let send_res = match state.clients.get_mut(&client_id.0) {
        Some(client) => {
            let reply = yserver_protocol::x11::mit_shm::encode_create_segment_reply(
                client.byte_order,
                sequence,
            );
            send_reply_with_fd(client, &reply, fd_for_client)
        }
        None => Ok(()),
    };
    unsafe { libc::close(fd_for_client) };
    send_res?;
    Ok(RequestOutcome::Handled)
}

fn handle_damage_request(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::damage as x11damage;
    let minor = header.data;
    match minor {
        x11damage::QUERY_VERSION => {
            let (client_major, client_minor) = x11damage::parse_query_version(body)
                .unwrap_or((x11damage::MAJOR_VERSION, x11damage::MINOR_VERSION));
            let major = x11damage::MAJOR_VERSION.min(client_major);
            let minor_ver = if major < x11damage::MAJOR_VERSION {
                client_minor
            } else {
                x11damage::MINOR_VERSION.min(client_minor)
            };
            debug!(
                "client {} #{} DAMAGE::QueryVersion -> {}.{}",
                client_id.0, sequence.0, major, minor_ver
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let byte_order = client.byte_order;
            let reply =
                x11damage::encode_query_version_reply(byte_order, sequence, major, minor_ver);
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11damage::CREATE => {
            if let Some((damage, drawable, level)) = x11damage::parse_create(body) {
                state.damage_objects.insert(
                    damage,
                    crate::server::DamageObject {
                        owner: client_id,
                        drawable: ResourceId(drawable),
                        level,
                        rects: Vec::new(),
                        pending_notify_fired: false,
                    },
                );
            }
            debug!("client {} #{} DAMAGE::Create", client_id.0, sequence.0);
        }
        x11damage::DESTROY => {
            if let Some(damage) = x11damage::parse_resource(body) {
                state.damage_objects.remove(&damage);
            }
            debug!("client {} #{} DAMAGE::Destroy", client_id.0, sequence.0);
        }
        x11damage::ADD => {
            if let Some((drawable, region)) = x11damage::parse_add(body) {
                let rects = if region == 0 {
                    vec![drawable_full_rect_xfixes(state, ResourceId(drawable))]
                } else {
                    state
                        .xfixes_regions
                        .get(&region)
                        .map(|r| r.rects.clone())
                        .unwrap_or_default()
                };
                for damage in state.damage_objects.values_mut() {
                    if damage.drawable == ResourceId(drawable) {
                        damage.rects.extend(rects.clone());
                        damage.rects = normalize_region_rects(std::mem::take(&mut damage.rects));
                    }
                }
            }
            debug!("client {} #{} DAMAGE::Add", client_id.0, sequence.0);
        }
        x11damage::SUBTRACT => {
            if let Some((damage_id, repair, parts)) = x11damage::parse_subtract(body) {
                let rects = state
                    .damage_objects
                    .get(&damage_id)
                    .map(|d| d.rects.clone())
                    .unwrap_or_default();
                if repair != 0 {
                    state.xfixes_regions.insert(
                        repair,
                        crate::server::XFixesRegion {
                            owner: client_id,
                            rects: rects.clone(),
                        },
                    );
                }
                if parts != 0 {
                    state.xfixes_regions.insert(
                        parts,
                        crate::server::XFixesRegion {
                            owner: client_id,
                            rects: Vec::new(),
                        },
                    );
                }
                if let Some(damage) = state.damage_objects.get_mut(&damage_id) {
                    damage.rects.clear();
                    damage.pending_notify_fired = false;
                }
            }
            debug!("client {} #{} DAMAGE::Subtract", client_id.0, sequence.0);
        }
        other => {
            debug!(
                "client {} #{} DAMAGE::unknown minor={}",
                client_id.0, sequence.0, other
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

fn handle_xtest_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::xtest as x11xtest;
    let minor = header.data;
    match minor {
        x11xtest::GET_VERSION => {
            let (cmaj, cmin) = x11xtest::parse_get_version(body).unwrap_or((
                u8::try_from(x11xtest::MAJOR_VERSION).unwrap_or(2),
                x11xtest::MINOR_VERSION,
            ));
            debug!(
                "client {} #{} XTEST::GetVersion client={cmaj}.{cmin} -> {}.{}",
                client_id.0,
                sequence.0,
                x11xtest::MAJOR_VERSION,
                x11xtest::MINOR_VERSION,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let byte_order = client.byte_order;
            let reply = x11xtest::encode_get_version_reply(
                byte_order,
                sequence,
                u8::try_from(x11xtest::MAJOR_VERSION).unwrap_or(2),
                x11xtest::MINOR_VERSION,
            );
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11xtest::COMPARE_CURSOR => {
            // Stub: report cursors as matching. xts cursor-comparison
            // tests will fail on real semantics; that's the baseline.
            debug!(
                "client {} #{} XTEST::CompareCursor (stub: same=true)",
                client_id.0, sequence.0
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let byte_order = client.byte_order;
            let reply = x11xtest::encode_compare_cursor_reply(byte_order, sequence, true);
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11xtest::FAKE_INPUT => {
            let Some(fi) = x11xtest::parse_fake_input(body) else {
                debug!(
                    "client {} #{} XTEST::FakeInput body too short ({} bytes), dropping",
                    client_id.0,
                    sequence.0,
                    body.len()
                );
                return Ok(RequestOutcome::Handled);
            };
            debug!(
                "client {} #{} XTEST::FakeInput type={} detail={} root_xy=({},{})",
                client_id.0, sequence.0, fi.event_type, fi.detail, fi.root_x, fi.root_y
            );
            dispatch_fake_input(state, backend, fi);
        }
        x11xtest::GRAB_CONTROL => {
            debug!(
                "client {} #{} XTEST::GrabControl (no-op)",
                client_id.0, sequence.0
            );
        }
        other => {
            debug!(
                "client {} #{} XTEST::unknown minor={}",
                client_id.0, sequence.0, other
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

fn dispatch_fake_input(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    fi: yserver_protocol::x11::xtest::FakeInput,
) {
    use crate::{core_loop::HostInputEvent, host_x11::HostKeyEvent};
    use yserver_protocol::x11::xtest as x11xtest;

    match fi.event_type {
        x11xtest::FAKE_KEY_PRESS | x11xtest::FAKE_KEY_RELEASE => {
            let pressed = fi.event_type == x11xtest::FAKE_KEY_PRESS;
            backend.on_host_input(
                state,
                HostInputEvent::Key(HostKeyEvent {
                    pressed,
                    keycode: fi.detail,
                    time: fi.time,
                    root_x: fi.root_x,
                    root_y: fi.root_y,
                    event_x: 0,
                    event_y: 0,
                    state: 0,
                }),
            );
        }
        x11xtest::FAKE_BUTTON_PRESS | x11xtest::FAKE_BUTTON_RELEASE => {
            let pressed = fi.event_type == x11xtest::FAKE_BUTTON_PRESS;
            // Translate X button number → Linux input code, since both
            // backends' `on_host_input` paths consume Linux codes.
            let linux_code = match fi.detail {
                1 => 0x110, // BTN_LEFT
                2 => 0x112, // BTN_MIDDLE
                3 => 0x111, // BTN_RIGHT
                8 => 0x113, // BTN_SIDE
                9 => 0x114, // BTN_EXTRA
                _ => {
                    log::debug!(
                        "XTEST FakeInput: dropping button event for unsupported X button {}",
                        fi.detail
                    );
                    return;
                }
            };
            backend.on_host_input(
                state,
                HostInputEvent::PointerButton {
                    button: linux_code,
                    pressed,
                    time: fi.time,
                },
            );
        }
        x11xtest::FAKE_MOTION_NOTIFY => {
            // detail==0 means absolute coords; detail==1 means relative.
            // We only support absolute for now — relative needs the
            // backend's current cursor position which isn't on the trait
            // surface. xts does most motion as absolute.
            if fi.detail != 0 {
                log::debug!("XTEST FakeInput: relative MotionNotify not supported, dropping");
                return;
            }
            backend.on_host_input(
                state,
                HostInputEvent::PointerMotion {
                    x: i32::from(fi.root_x),
                    y: i32::from(fi.root_y),
                    time: fi.time,
                },
            );
        }
        other => {
            log::debug!("XTEST FakeInput: unknown event type {other}, dropping");
        }
    }
}

#[allow(clippy::too_many_arguments)]
fn handle_present_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::{ClientByteOrder, present as x11present};
    const PRESENT_MAJOR_OPCODE: u8 = 145;
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    match minor {
        x11present::QUERY_VERSION => {
            let _ = x11present::parse_query_version(body);
            debug!(
                "client {} #{} PRESENT::QueryVersion -> {}.{}",
                client_id.0,
                sequence.0,
                x11present::MAJOR_VERSION,
                x11present::MINOR_VERSION
            );
            let reply = x11present::encode_query_version_reply(
                byte_order,
                sequence,
                x11present::MAJOR_VERSION,
                x11present::MINOR_VERSION,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11present::QUERY_CAPABILITIES => {
            let _target = x11present::parse_query_capabilities(body).unwrap_or(0);
            debug!(
                "client {} #{} PRESENT::QueryCapabilities -> none",
                client_id.0, sequence.0
            );
            let reply = x11present::encode_query_capabilities_reply(
                byte_order,
                sequence,
                x11present::CAPABILITY_NONE,
            );
            let Some(client) = state.clients.get_mut(&client_id.0) else {
                return Ok(RequestOutcome::Handled);
            };
            let _byte_order = client.byte_order;
            return Ok(write_to_client(client, client_id, &reply));
        }
        x11present::SELECT_INPUT => {
            if let Some(req) = x11present::parse_select_input(body) {
                state.present_event_selections.insert(
                    req.eid,
                    crate::server::PresentEventSelection {
                        owner: client_id,
                        window: ResourceId(req.window),
                        event_mask: req.event_mask,
                    },
                );
            }
            debug!(
                "client {} #{} PRESENT::SelectInput",
                client_id.0, sequence.0
            );
        }
        x11present::PIXMAP => {
            let Some(req) = x11present::parse_pixmap(body) else {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_LENGTH,
                    0,
                    PRESENT_MAJOR_OPCODE,
                );
            };
            if req.wait_fence != 0 || req.idle_fence != 0 {
                let bad = if req.wait_fence != 0 {
                    req.wait_fence
                } else {
                    req.idle_fence
                };
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_IMPLEMENTATION,
                    bad,
                    PRESENT_MAJOR_OPCODE,
                );
            }
            let window_exists = state.resources.window(ResourceId(req.window)).is_some();
            let pixmap_exists = state.resources.pixmap(ResourceId(req.pixmap)).is_some();
            let src = state.resources.host_drawable_target(ResourceId(req.pixmap));
            let dst = state.resources.host_drawable_target(ResourceId(req.window));
            if !window_exists {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_WINDOW,
                    req.window,
                    PRESENT_MAJOR_OPCODE,
                );
            }
            if !pixmap_exists {
                return emit_x11_error(
                    state,
                    client_id,
                    sequence,
                    x11::error::BAD_DRAWABLE,
                    req.pixmap,
                    PRESENT_MAJOR_OPCODE,
                );
            }
            if let (
                Some(crate::resources::HostDrawableTarget::Pixmap {
                    host_xid,
                    width,
                    height,
                    depth: src_depth,
                    ..
                }),
                Some(dst),
            ) = (src, dst)
            {
                if src_depth != dst.depth() {
                    return emit_x11_error(
                        state,
                        client_id,
                        sequence,
                        x11::error::BAD_MATCH,
                        req.pixmap,
                        PRESENT_MAJOR_OPCODE,
                    );
                }
                backend.copy_area(
                    origin,
                    host_xid.as_raw(),
                    dst.host_xid(),
                    req.x_off,
                    req.y_off,
                    0,
                    0,
                    width,
                    height,
                )?;
            }
            state
                .present_msc
                .entry(ResourceId(req.window))
                .and_modify(|msc| *msc = msc.saturating_add(1))
                .or_insert(1);
            debug!(
                "client {} #{} PRESENT::Pixmap serial={} notifies={}",
                client_id.0,
                sequence.0,
                req.serial,
                req.notifies.len()
            );
        }
        x11present::NOTIFY_MSC => {
            if let Some(req) = x11present::parse_notify_msc(body) {
                state
                    .present_msc
                    .entry(ResourceId(req.window))
                    .and_modify(|msc| *msc = (*msc).max(req.target_msc).saturating_add(1))
                    .or_insert(req.target_msc.saturating_add(1));
            }
            debug!("client {} #{} PRESENT::NotifyMSC", client_id.0, sequence.0);
        }
        x11present::PIXMAP_SYNCED => {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_IMPLEMENTATION,
                0,
                PRESENT_MAJOR_OPCODE,
            );
        }
        _ => {
            debug!(
                "client {} #{} PRESENT unsupported minor {}",
                client_id.0, sequence.0, minor,
            );
        }
    }
    Ok(RequestOutcome::Handled)
}

fn handle_xi2_request(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    use yserver_protocol::x11::ClientByteOrder;
    let byte_order = state
        .clients
        .get(&client_id.0)
        .map_or(ClientByteOrder::LittleEndian, |c| c.byte_order);
    let minor = header.data;
    let mut buf: Vec<u8> = Vec::with_capacity(64);
    match minor {
        1 => {
            // XI GetExtensionVersion
            debug!(
                "client {} #{} XIGetExtensionVersion",
                client_id.0, sequence.0
            );
            let mut reply = x11::fixed_reply(byte_order, sequence, 0, 0);
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 2);
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 0);
            reply.push(1);
            reply.extend_from_slice(&[0; 19]);
            buf.extend_from_slice(&reply);
        }
        42 => {
            debug!("client {} #{} XIChangeCursor", client_id.0, sequence.0);
            return Ok(RequestOutcome::Handled);
        }
        44 => {
            debug!("client {} #{} XISetClientPointer", client_id.0, sequence.0);
            return Ok(RequestOutcome::Handled);
        }
        45 => {
            debug!("client {} #{} XIGetClientPointer", client_id.0, sequence.0);
            let mut reply = x11::fixed_reply(byte_order, sequence, 0, 0);
            reply.push(1);
            reply.push(0);
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 2);
            reply.extend_from_slice(&[0; 22]);
            buf.extend_from_slice(&reply);
        }
        46 => {
            debug!("client {} #{} XISelectEvents", client_id.0, sequence.0);
            if body.len() >= 8 {
                let window = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
                let num_masks = u16::from_le_bytes([body[4], body[5]]) as usize;
                let mut pos = 8;
                if let Some(client) = state.clients.get_mut(&client_id.0) {
                    for _ in 0..num_masks {
                        if pos + 4 > body.len() {
                            break;
                        }
                        let deviceid = u16::from_le_bytes([body[pos], body[pos + 1]]);
                        let mask_len = u16::from_le_bytes([body[pos + 2], body[pos + 3]]) as usize;
                        pos += 4;
                        let byte_len = mask_len.saturating_mul(4);
                        if pos + byte_len > body.len() {
                            break;
                        }
                        let mask = if mask_len > 0 {
                            u32::from_le_bytes([
                                body[pos],
                                body[pos + 1],
                                body[pos + 2],
                                body[pos + 3],
                            ])
                        } else {
                            0
                        };
                        debug!(
                            "client {} XISelectEvents window=0x{:x} deviceid={} mask=0x{:x}",
                            client_id.0, window.0, deviceid, mask
                        );
                        if mask == 0 {
                            client.xi2_masks.remove(&(window, deviceid));
                        } else {
                            client.xi2_masks.insert((window, deviceid), mask);
                        }
                        pos += byte_len;
                    }
                }
            }
            return Ok(RequestOutcome::Handled);
        }
        47 => {
            debug!("client {} #{} XIQueryVersion", client_id.0, sequence.0);
            let mut reply = x11::fixed_reply(byte_order, sequence, 0, 0);
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 2);
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 2);
            reply.extend_from_slice(&[0; 20]);
            buf.extend_from_slice(&reply);
        }
        48 => {
            debug!("client {} #{} XIQueryDevice", client_id.0, sequence.0);
            let mut infos = Vec::new();
            for (deviceid, use_, attachment, name) in [
                (2u16, 1u16, 3u16, "Virtual core pointer"),
                (3u16, 2u16, 2u16, "Virtual core keyboard"),
            ] {
                x11::write_u16(ClientByteOrder::LittleEndian, &mut infos, deviceid);
                x11::write_u16(ClientByteOrder::LittleEndian, &mut infos, use_);
                x11::write_u16(ClientByteOrder::LittleEndian, &mut infos, attachment);
                x11::write_u16(ClientByteOrder::LittleEndian, &mut infos, 0);
                x11::write_u16(ClientByteOrder::LittleEndian, &mut infos, name.len() as u16);
                infos.push(1);
                infos.push(0);
                infos.extend_from_slice(name.as_bytes());
                x11::pad_vec4(&mut infos);
            }
            let mut reply = x11::fixed_reply(
                byte_order,
                sequence,
                0,
                x11::checked_units(infos.len())? as u32,
            );
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 2);
            reply.extend_from_slice(&[0; 22]);
            reply.extend_from_slice(&infos);
            buf.extend_from_slice(&reply);
        }
        59 => {
            debug!(
                "client {} #{} XIGetProperty -> not found",
                client_id.0, sequence.0
            );
            let mut reply = x11::fixed_reply(byte_order, sequence, 0, 0);
            reply.extend_from_slice(&[0u8; 24]);
            buf.extend_from_slice(&reply);
        }
        60 => {
            debug!("client {} #{} XIGetSelectedEvents", client_id.0, sequence.0);
            if body.len() < 4 {
                return Ok(RequestOutcome::Handled);
            }
            let window = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
            let mut masks = Vec::new();
            if let Some(client) = state.clients.get(&client_id.0) {
                for (&(win, dev), &mask) in &client.xi2_masks {
                    if win == window {
                        x11::write_u16(ClientByteOrder::LittleEndian, &mut masks, dev);
                        x11::write_u16(ClientByteOrder::LittleEndian, &mut masks, 1);
                        x11::write_u32(ClientByteOrder::LittleEndian, &mut masks, mask);
                    }
                }
            }
            let num_masks = (masks.len() / 8) as u16;
            let mut reply = x11::fixed_reply(
                byte_order,
                sequence,
                0,
                x11::checked_units(masks.len())? as u32,
            );
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, num_masks);
            reply.extend_from_slice(&[0; 22]);
            reply.extend_from_slice(&masks);
            buf.extend_from_slice(&reply);
        }
        40 => {
            debug!("client {} #{} XIQueryPointer", client_id.0, sequence.0);
            let mut reply = x11::fixed_reply(byte_order, sequence, 1, 6);
            x11::write_u32(ClientByteOrder::LittleEndian, &mut reply, ROOT_WINDOW.0);
            x11::write_u32(ClientByteOrder::LittleEndian, &mut reply, 0);
            x11::write_u32(ClientByteOrder::LittleEndian, &mut reply, 0);
            x11::write_u32(ClientByteOrder::LittleEndian, &mut reply, 0);
            x11::write_u32(ClientByteOrder::LittleEndian, &mut reply, 0);
            x11::write_u32(ClientByteOrder::LittleEndian, &mut reply, 0);
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 0);
            x11::write_u16(ClientByteOrder::LittleEndian, &mut reply, 0);
            reply.extend_from_slice(&[0u8; 16]);
            reply.extend_from_slice(&[0u8; 4]);
            buf.extend_from_slice(&reply);
        }
        // XI 1.x reply-required minors. xts opens a probe XListInputDevices /
        // XOpenDevice on every test, so without these stubs the entire
        // XI / XIproto suites hang on _XReply. Each reply is exactly
        // 32 bytes (the standard reply header + 24 zero bytes); all
        // count/status fields default to 0 which means
        // "empty list" / "Success" in their respective contexts.
        2 |  // ListInputDevices: ndevices = 0
        3 |  // OpenDevice: num_classes = 0
        5 |  // SetDeviceMode: status = Success
        7 |  // GetSelectedExtensionEvents: counts = 0
        9 |  // GetDeviceDontPropagateList: count = 0
        10 | // GetDeviceMotionEvents: nEvents = 0
        11 | // ChangeKeyboardDevice: status = Success
        12 | // ChangePointerDevice: status = Success
        13 | // GrabDevice: status = GrabSuccess
        20 | // GetDeviceFocus: focus = None
        22 | // GetFeedbackControl: num_feedbacks = 0
        24 | // GetDeviceKeyMapping: keysyms_per_keycode = 0
        26 | // GetDeviceModifierMapping: numKeyPerModifier = 0
        27 | // SetDeviceModifierMapping: success = MappingSuccess
        28 | // GetDeviceButtonMapping: nElts = 0
        29 | // SetDeviceButtonMapping: status = MappingSuccess
        30 | // QueryDeviceState: num_classes = 0
        33 | // SetDeviceValuators: status = Success
        34 | // GetDeviceControl: status = Success
        35 | // ChangeDeviceControl: status = Success
        36 | // ListDeviceProperties: nAtoms = 0
        39   // GetDeviceProperty: propertyType = None
        => {
            debug!("client {} #{} XI 1.x stub minor={}", client_id.0, sequence.0, minor);
            let mut reply = x11::fixed_reply(byte_order, sequence, 0, 0);
            reply.extend_from_slice(&[0u8; 24]);
            buf.extend_from_slice(&reply);
        }
        _ => {
            debug!("unhandled XI2 request minor={}", minor);
            return Ok(RequestOutcome::Handled);
        }
    }
    let Some(client) = state.clients.get_mut(&client_id.0) else {
        return Ok(RequestOutcome::Handled);
    };
    let _byte_order = client.byte_order;
    Ok(write_to_client(client, client_id, &buf))
}

#[allow(clippy::too_many_arguments)]
fn handle_xkb_request(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let minor = header.data;
    debug!(
        "client {} #{} XkbProxy minor={}",
        client_id.0, sequence.0, minor
    );
    if minor == 1 && body.len() >= 12 {
        let device_spec = u16::from_le_bytes([body[0], body[1]]);
        let clear = u16::from_le_bytes([body[4], body[5]]);
        let select_all = u16::from_le_bytes([body[6], body[7]]);
        let selected = select_all & !clear;
        if selected == 0 {
            state
                .xkb_select_event_masks
                .remove(&(client_id.0, device_spec));
        } else {
            state
                .xkb_select_event_masks
                .insert((client_id.0, device_spec), selected);
        }
    }
    let reply = backend.xkb_proxy(origin, minor, body).ok().flatten();
    if let Some(mut bytes) = reply {
        if bytes.len() >= 4 {
            bytes[2..4].copy_from_slice(&sequence.0.to_le_bytes());
        }
        let Some(client) = state.clients.get_mut(&client_id.0) else {
            return Ok(RequestOutcome::Handled);
        };
        let _byte_order = client.byte_order;
        return Ok(write_to_client(client, client_id, &bytes));
    }
    Ok(RequestOutcome::Handled)
}

fn handle_reparent_window(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let Some(request) = x11::reparent_window_request(body) else {
        return Ok(RequestOutcome::Handled);
    };
    let result = match state.resources.reparent_window(request) {
        Ok(result) => result,
        Err(crate::resources::ReparentWindowError::BadWindow) => {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_WINDOW,
                request.window.0,
                7,
            );
        }
        Err(crate::resources::ReparentWindowError::BadMatch) => {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_MATCH,
                request.window.0,
                7,
            );
        }
    };
    let on_window = subscribers_by_id(state, result.window, 0x0002_0000);
    let on_old_parent = subscribers_by_id(state, result.old_parent, 0x0008_0000);
    let on_new_parent = if result.old_parent == result.new_parent {
        Vec::new()
    } else {
        subscribers_by_id(state, result.new_parent, 0x0008_0000)
    };
    debug!(
        "client {} #{} ReparentWindow 0x{:x}: 0x{:x}->0x{:x} pos=({},{}) host_xid={:?}",
        client_id.0,
        sequence.0,
        result.window.0,
        result.old_parent.0,
        result.new_parent.0,
        result.x,
        result.y,
        result.host_xid
    );
    if let Some(xid) = result.host_xid {
        let new_host_parent = if result.new_parent == ROOT_WINDOW {
            Some(backend.window_id())
        } else {
            state
                .resources
                .window(result.new_parent)
                .and_then(|w| w.host_xid)
                .map(|h| h.as_raw())
        };
        if let Some(host_parent) = new_host_parent {
            let _ =
                backend.reparent_subwindow(origin, xid.as_raw(), host_parent, result.x, result.y);
        }
        if result.old_parent == ROOT_WINDOW && result.new_parent != ROOT_WINDOW {
            let _ = backend.update_host_event_mask(
                origin,
                xid.as_raw(),
                crate::host_x11::POINTER_EVENT_MASK,
                false,
            );
            if let Err(err) = backend.register_subwindow(origin, result.window, xid.as_raw()) {
                log::warn!(
                    "client {} register_subwindow for 0x{:x} on reparent failed: {err}",
                    client_id.0,
                    result.window.0
                );
            }
        }
        if result.old_parent != ROOT_WINDOW && result.new_parent == ROOT_WINDOW {
            let _ = backend.update_host_event_mask(
                origin,
                xid.as_raw(),
                crate::host_x11::SUBWINDOW_EVENT_MASK,
                false,
            );
            if let Err(err) = backend.register_top_level(origin, result.window, xid.as_raw()) {
                log::warn!(
                    "client {} register_top_level for 0x{:x} on reparent failed: {err}",
                    client_id.0,
                    result.window.0
                );
            }
        }
    }
    let window = result.window;
    let new_parent = result.new_parent;
    let old_parent = result.old_parent;
    let rx = result.x;
    let ry = result.y;
    let override_redirect = result.override_redirect;
    let _dropped = fanout_event_to_clients(state, &on_window, |buf, seq, order| {
        x11::encode_reparent_notify_event(
            buf,
            seq,
            order,
            window,
            window,
            new_parent,
            rx,
            ry,
            override_redirect,
        );
    });
    let _dropped = fanout_event_to_clients(state, &on_old_parent, |buf, seq, order| {
        x11::encode_reparent_notify_event(
            buf,
            seq,
            order,
            old_parent,
            window,
            new_parent,
            rx,
            ry,
            override_redirect,
        );
    });
    let _dropped = fanout_event_to_clients(state, &on_new_parent, |buf, seq, order| {
        x11::encode_reparent_notify_event(
            buf,
            seq,
            order,
            new_parent,
            window,
            new_parent,
            rx,
            ry,
            override_redirect,
        );
    });
    Ok(RequestOutcome::Handled)
}

#[allow(clippy::too_many_arguments)]
fn handle_create_window(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let Some(request) = x11::create_window_request(header.data, body) else {
        return Ok(RequestOutcome::Handled);
    };
    debug!(
        "client {} create window 0x{:x} parent=0x{:x} pos=({},{}) size={}x{} mask=0x{:x}",
        client_id.0,
        request.window.0,
        request.parent.0,
        request.x,
        request.y,
        request.width,
        request.height,
        request.event_mask.unwrap_or(0)
    );
    let new_id = request.window.0;
    let mask = request.event_mask.unwrap_or(0);
    let window_id = request.window;
    let parent = request.parent;
    let geometry = (request.x, request.y, request.width, request.height);
    // BadIdChoice / BadMatch validation.
    let validation_failed = {
        let handle = state.clients.get(&client_id.0).expect("client registered");
        let owned = crate::server::IdAllocator::validate_owned(
            new_id,
            handle.resource_id_base,
            handle.resource_id_mask,
        );
        let in_use = state.resources.any_resource_exists(request.window);
        !owned || in_use
    };
    if validation_failed {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_ID_CHOICE,
            new_id,
            1,
        );
    }
    let visual_known = request.visual.0 == 0 || state.resources.is_known_visual(request.visual);
    if !visual_known {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_MATCH,
            request.visual.0,
            1,
        );
    }
    state.resources.create_window(client_id, request);
    if mask != 0 {
        state
            .clients
            .get_mut(&client_id.0)
            .expect("client registered")
            .event_masks
            .insert(window_id, mask);
    }
    let needs_host_xid = state
        .resources
        .window(window_id)
        .is_some_and(|w| w.class != crate::resources::WindowClass::InputOnly);
    if needs_host_xid {
        let host_visual = resolve_host_subwindow_visual_to_state(state, window_id);
        let host_parent: Option<crate::backend::WindowHandle> = if parent == ROOT_WINDOW {
            crate::backend::WindowHandle::from_raw(backend.window_id())
        } else {
            state.resources.window(parent).and_then(|w| w.host_xid)
        };
        let local = state.resources.window(window_id);
        let host_bg_pixel = local.map(|w| w.background_pixel);
        let host_bg_pixmap = local
            .and_then(|w| w.background_pixmap_host_xid)
            .map(|h| h.as_raw());
        let allocated = host_parent.and_then(|host_parent| {
            match backend.create_subwindow(
                origin,
                host_parent,
                geometry.0,
                geometry.1,
                geometry.2,
                geometry.3,
                request.border_width,
                host_visual,
                host_bg_pixel,
                host_bg_pixmap,
            ) {
                Ok(handle) => Some(handle),
                Err(err) => {
                    log::warn!(
                        "client {} create_subwindow for 0x{:x} failed: {err}",
                        client_id.0,
                        new_id
                    );
                    None
                }
            }
        });
        if let Some(host_handle) = allocated {
            if let Some(w) = state.resources.window_mut(window_id) {
                w.host_xid = Some(host_handle);
            }
            let host_xid = host_handle.as_raw();
            let result = if parent == ROOT_WINDOW {
                backend.register_top_level(origin, window_id, host_xid)
            } else {
                backend.register_subwindow(origin, window_id, host_xid)
            };
            if let Err(err) = result {
                log::warn!(
                    "client {} register host window for 0x{:x} failed: {err}",
                    client_id.0,
                    new_id
                );
            }
        }
    }
    let wants_focus = {
        let mask = state
            .clients
            .get(&client_id.0)
            .and_then(|c| c.event_masks.get(&window_id).copied())
            .unwrap_or(0);
        let viewable = state
            .resources
            .window(window_id)
            .is_some_and(|w| w.map_state == MapState::Viewable);
        viewable && (mask & 0x3) != 0
    };
    if wants_focus {
        set_focused_window_to_state(state, client_id, window_id);
    }
    // CreateNotify on parent (SubstructureNotify subscribers).
    let create_notify_targets = subscribers_by_id(state, parent, 0x0008_0000);
    if !create_notify_targets.is_empty() {
        let geometry_opt = state.resources.window(window_id).map(window_geometry);
        if let Some(geometry) = geometry_opt {
            let override_redir = request.override_redirect.unwrap_or(false);
            let _dropped =
                fanout_event_to_clients(state, &create_notify_targets, |buf, seq, order| {
                    x11::encode_create_notify_event(
                        buf,
                        seq,
                        order,
                        parent,
                        window_id,
                        geometry,
                        override_redir,
                    );
                });
        }
    }
    debug!("client {} #{} CreateWindow", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

fn handle_change_window_attributes(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let Some(request) = x11::change_window_attributes_request(body) else {
        return Ok(RequestOutcome::Handled);
    };
    if let Some(event_mask) = request.event_mask {
        debug!(
            "client {} attrs window 0x{:x} mask=0x{:x}",
            client_id.0, request.window.0, event_mask
        );
        let entry = state
            .clients
            .get_mut(&client_id.0)
            .expect("client registered");
        if event_mask == 0 {
            entry.event_masks.remove(&request.window);
        } else {
            entry.event_masks.insert(request.window, event_mask);
        }
    }
    let target_window = request.window;
    let cursor_id = request.cursor;
    let previous_bg_host_xid = state.resources.change_window_attributes(request);
    if let Some(old_host_xid) = previous_bg_host_xid
        && !state
            .resources
            .host_xid_referenced_by_window_bg(old_host_xid)
    {
        let _ = backend.free_pixmap(origin, old_host_xid.as_raw());
    }

    let want_focus_check = state
        .clients
        .get(&client_id.0)
        .and_then(|c| c.event_masks.get(&target_window).copied())
        .unwrap_or(0);
    let viewable = state
        .resources
        .window(target_window)
        .is_some_and(|w| w.map_state == MapState::Viewable);

    if target_window == ROOT_WINDOW {
        let root_bg_host_xid = if request.background_pixmap.is_some() {
            state
                .resources
                .window_background_pixmap_host_xid(ROOT_WINDOW)
        } else {
            None
        };
        debug!(
            "client {} CWA(root) bg_pixmap={:?} bg_pixel={:?} root_bg_host_xid={:?}",
            client_id.0, request.background_pixmap, request.background_pixel, root_bg_host_xid,
        );
        if request.background_pixmap.is_some() {
            let _ = backend.set_container_background_pixmap(origin, root_bg_host_xid.unwrap_or(0));
        } else if let Some(pixel) = request.background_pixel {
            let _ = backend.set_container_background_pixel(origin, pixel);
        }
    }

    if viewable && want_focus_check & 0x3 != 0 {
        set_focused_window_to_state(state, client_id, target_window);
    }

    if target_window != ROOT_WINDOW
        && (request.background_pixel.is_some() || request.background_pixmap.is_some())
    {
        let host_xid = state
            .resources
            .window(target_window)
            .and_then(|w| w.host_xid);
        let bg_pixel = state
            .resources
            .window(target_window)
            .map(|w| w.background_pixel);
        let bg_pixmap_host_xid = state
            .resources
            .window(target_window)
            .and_then(|w| w.background_pixmap_host_xid);
        if let Some(host_xid) = host_xid {
            let mut value_mask: u32 = 0;
            let mut values: Vec<u32> = Vec::with_capacity(1);
            if request.background_pixmap.is_some() {
                value_mask |= 1 << 0;
                values.push(bg_pixmap_host_xid.map(|h| h.as_raw()).unwrap_or(0));
            } else if request.background_pixel.is_some() {
                value_mask |= 1 << 1;
                values.push(bg_pixel.unwrap_or(0));
            }
            let _ =
                backend.change_subwindow_attributes(origin, host_xid.as_raw(), value_mask, &values);
        }
    }

    if let Some(cid) = cursor_id {
        let host_window_raw = if target_window == ROOT_WINDOW {
            Some(backend.window_id())
        } else {
            state
                .resources
                .window(target_window)
                .and_then(|w| w.host_xid)
                .map(|w| w.as_raw())
        };
        let cursor_host_xid = state.resources.cursor_host_xid(cid);
        if let (Some(hw), Some(ch)) = (host_window_raw, cursor_host_xid) {
            let _ = backend.define_cursor(origin, hw, ch);
        }
    }
    debug!(
        "client {} #{} ChangeWindowAttributes",
        client_id.0, sequence.0
    );
    Ok(RequestOutcome::Handled)
}

fn handle_configure_window(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let Some(request) = x11::configure_window_request(body) else {
        return Ok(RequestOutcome::Handled);
    };
    let pre = state
        .resources
        .window(request.window)
        .map(|w| (w.parent, w.override_redirect));
    let redirect_targets: Vec<ClientId> = if let Some((parent, false)) = pre {
        let requester_has = state
            .clients
            .get(&client_id.0)
            .and_then(|c| c.event_masks.get(&parent).copied())
            .is_some_and(|m| m & 0x0010_0000 != 0);
        if requester_has {
            Vec::new()
        } else {
            subscribers_by_id(state, parent, 0x0010_0000)
        }
    } else {
        Vec::new()
    };
    if !redirect_targets.is_empty() {
        let parent = pre.map(|(p, _)| p).unwrap_or(ROOT_WINDOW);
        let _dropped = fanout_event_to_clients(state, &redirect_targets, |buf, seq, order| {
            x11::encode_configure_request_event(buf, seq, order, parent, request.window, &request);
        });
        return Ok(RequestOutcome::Handled);
    }
    let old_size = state
        .resources
        .window(request.window)
        .map(|w| (w.width, w.height));
    let sibling_host_xid = request
        .sibling
        .and_then(|sibling| state.resources.window(sibling))
        .and_then(|w| w.host_xid)
        .map(|h| h.as_raw());
    let configure = state
        .resources
        .configure_window(request)
        .map(|w| (w.id, window_geometry(w), w.override_redirect));
    let host_xid = configure
        .as_ref()
        .and_then(|(id, _, _)| state.resources.window(*id).and_then(|w| w.host_xid));
    let parent = configure
        .as_ref()
        .and_then(|(id, _, _)| state.resources.window(*id).map(|w| w.parent));
    debug!(
        "client {} #{} ConfigureWindow 0x{:x} mask=0x{:x} host_xid={:?}",
        client_id.0, sequence.0, request.window.0, request.value_mask, host_xid
    );
    if let Some(xid) = host_xid {
        let _ = backend.configure_subwindow(
            origin,
            xid.as_raw(),
            crate::host_x11::HostSubwindowConfig {
                x: request.x,
                y: request.y,
                width: request.width,
                height: request.height,
                border_width: request.border_width,
                sibling: sibling_host_xid,
                stack_mode: request.stack_mode,
            },
        );
    }
    if let Some((window_id, geometry, override_redirect)) = configure {
        let _dropped =
            emit_window_event_to_state(state, window_id, 0x0002_0000, |buf, seq, order| {
                x11::encode_configure_notify_event(
                    buf,
                    seq,
                    order,
                    window_id,
                    window_id,
                    geometry,
                    override_redirect,
                );
            });
        if let Some(parent) = parent {
            let _dropped =
                emit_window_event_to_state(state, parent, 0x0008_0000, |buf, seq, order| {
                    x11::encode_configure_notify_event(
                        buf,
                        seq,
                        order,
                        parent,
                        window_id,
                        geometry,
                        override_redirect,
                    );
                });
        }
        let resized =
            old_size.is_some_and(|(ow, oh)| geometry.width != ow || geometry.height != oh);
        if resized {
            invalidate_composite_named_pixmaps_to_state(state, backend, origin, window_id);
        }
        let grew = old_size.is_some_and(|(ow, oh)| geometry.width > ow || geometry.height > oh);
        if grew {
            let _dropped =
                emit_window_event_to_state(state, window_id, 0x0000_8000, |buf, seq, order| {
                    x11::encode_expose_event(
                        buf,
                        seq,
                        order,
                        window_id,
                        0,
                        0,
                        geometry.width,
                        geometry.height,
                        0,
                    );
                });
            let _dropped = emit_expose_subtree_to_state(state, window_id);
        }
    }
    Ok(RequestOutcome::Handled)
}

fn handle_destroy_window(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    if let Some(window) = x11::free_resource_id(body) {
        destroy_window_subtree(state, backend, origin, window);
    }
    debug!("client {} #{} DestroyWindow", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

fn handle_destroy_subwindows(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    if body.len() >= 4 {
        let parent = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
        let kids: Vec<ResourceId> = state.resources.children(parent).to_vec();
        for k in kids {
            destroy_window_subtree(state, backend, origin, k);
        }
    }
    debug!("client {} #{} DestroySubwindows", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

/// Returns `(major_opcode, first_event, first_error)` for an extension
/// the client just queried, or `None` if it isn't available. Mirrors
/// `nested::extension_query_reply` but operates on a live
/// `&mut dyn Backend` instead of `Option<&Arc<Mutex<dyn Backend>>>`.
fn extension_query_reply(name: &str, backend: &mut dyn Backend) -> Option<(u8, u8, u8)> {
    use crate::nested::{EXTENSIONS, ExtensionAvailability};
    let ext = EXTENSIONS.iter().find(|ext| ext.name == name)?;
    let available = match ext.availability {
        ExtensionAvailability::Always => true,
        ExtensionAvailability::HostRender => backend.render_opcode().is_some(),
        ExtensionAvailability::HostXkb => backend.xkb_opcode().is_some(),
    };
    if !available {
        return None;
    }
    if ext.availability == ExtensionAvailability::HostXkb {
        let (_, first_event, first_error) = backend.xkb_info()?;
        return Some((ext.major_opcode, first_event, first_error));
    }
    Some((ext.major_opcode, ext.first_event, ext.first_error))
}

fn advertised_extension_names(backend: &mut dyn Backend) -> Vec<&'static str> {
    use crate::nested::{EXTENSIONS, ExtensionAvailability};
    let render_available = backend.render_opcode().is_some();
    let xkb_available = backend.xkb_opcode().is_some();
    EXTENSIONS
        .iter()
        .filter(|ext| match ext.availability {
            ExtensionAvailability::Always => true,
            ExtensionAvailability::HostRender => render_available,
            ExtensionAvailability::HostXkb => xkb_available,
        })
        .map(|ext| ext.name)
        .collect()
}

/// Update `client.focused_window` and emit the FocusOut/FocusIn pair to
/// FocusChange-mask subscribers. Mirrors `nested::set_focused_window`
/// against the per-client `focused_window` field added in A1.
fn set_focused_window_to_state(state: &mut ServerState, client_id: ClientId, window: ResourceId) {
    if window == ResourceId(0) {
        log::trace!(
            "set_focused_window: client {} -> NONE (ignored)",
            client_id.0
        );
        return;
    }
    // Focus is a single global value in X11 — every client's
    // GetInputFocus should observe the same target. The
    // `ClientState::focused_window` field is mirrored across clients
    // (the docstring on `current_focus` describes this) so the key
    // fanout's walk-clients-find-first-non-root pick is well-defined.
    // Without the mirror, multi-client sessions where the WM and the
    // app both call SetInputFocus on their own internal windows make
    // `current_focus` return whichever client's row HashMap iteration
    // visits first — typically the WM, so keys never reach the app.
    let prev = match state.clients.get(&client_id.0) {
        Some(c) if c.focused_window == window => return,
        Some(c) => c.focused_window,
        None => return,
    };
    log::trace!(
        "set_focused_window: client {} prev=0x{:x} -> 0x{:x} (mirroring to {} clients)",
        client_id.0,
        prev.0,
        window.0,
        state.clients.len(),
    );
    for c in state.clients.values_mut() {
        c.focused_window = window;
    }
    if prev != ROOT_WINDOW {
        let _dropped =
            emit_window_event_to_state(state, prev, FOCUS_CHANGE_MASK, |buf, seq, order| {
                x11::encode_focus_event(buf, seq, order, false, prev);
            });
        let _dropped = emit_xi2_focus_event_to_state(state, prev, 10, XI2_MAJOR_OPCODE);
    }
    let _dropped =
        emit_window_event_to_state(state, window, FOCUS_CHANGE_MASK, |buf, seq, order| {
            x11::encode_focus_event(buf, seq, order, true, window);
        });
    let _dropped = emit_xi2_focus_event_to_state(state, window, 9, XI2_MAJOR_OPCODE);
}

fn clear_extent(requested: u16, offset: i16, window_extent: u16) -> u16 {
    if requested != 0 {
        return requested;
    }
    if offset <= 0 {
        window_extent
    } else {
        window_extent.saturating_sub(offset as u16)
    }
}

fn rewrite_reply_sequence(reply: &mut [u8], sequence: SequenceNumber) {
    if reply.len() >= 4 {
        let bytes = sequence.0.to_le_bytes();
        reply[2] = bytes[0];
        reply[3] = bytes[1];
    }
}

