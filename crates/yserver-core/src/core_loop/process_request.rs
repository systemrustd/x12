fn handle_clear_area(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let exposures = header.data != 0;
    if let Some(request) = x11::clear_area_request(body) {
        let extents = state
            .resources
            .window(request.window)
            .map(|w| (w.background_pixel, w.width, w.height));
        let bg_pixmap_host = state
            .resources
            .window_background_pixmap_host_xid(request.window);
        let target = state.resources.host_drawable_target(request.window);
        if let Some((background_pixel, w_width, w_height)) = extents
            && let Some(target) = target
        {
            let width = clear_extent(request.width, request.x, w_width);
            let height = clear_extent(request.height, request.y, w_height);
            if width != 0 && height != 0 {
                backend.clear_clip_rectangles(origin)?;
                if let Some(bg_host_xid) = bg_pixmap_host {
                    backend.copy_area(
                        origin,
                        bg_host_xid,
                        target.host_xid(),
                        request.x,
                        request.y,
                        request.x,
                        request.y,
                        width,
                        height,
                    )?;
                } else {
                    backend.fill_rectangle(
                        origin,
                        target.host_xid(),
                        background_pixel,
                        request.x,
                        request.y,
                        width,
                        height,
                    )?;
                }
                let _dropped = accumulate_damage_to_state(
                    state,
                    request.window,
                    request.x,
                    request.y,
                    width,
                    height,
                );
                // X11 spec: when `exposures` is True (header.data),
                // the server sends an Expose event for visible regions
                // of the cleared rectangle.
                if exposures {
                    let req = request;
                    let _dropped = emit_window_event_to_state(
                        state,
                        req.window,
                        0x0000_8000,
                        |buf, seq, order| {
                            x11::encode_expose_event(
                                buf,
                                seq,
                                order,
                                req.window,
                                req.x as u16,
                                req.y as u16,
                                width,
                                height,
                                0,
                            );
                        },
                    );
                }
            }
        }
    }
    debug!("client {} #{} ClearArea", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

fn handle_copy_area(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let Some(request) = x11::copy_area_request(body) else {
        return Ok(RequestOutcome::Handled);
    };
    if request.width == 0 || request.height == 0 {
        return Ok(RequestOutcome::Handled);
    }
    debug!(
        "client {} #{} CopyArea src=0x{:x} dst=0x{:x} gc=0x{:x} src=({},{}) dst=({},{}) {}x{}",
        client_id.0,
        sequence.0,
        request.src.0,
        request.dst.0,
        request.gc.0,
        request.src_x,
        request.src_y,
        request.dst_x,
        request.dst_y,
        request.width,
        request.height
    );
    let gc_exists = state.resources.gc(request.gc).is_some();
    let src_exists = state.resources.window(request.src).is_some()
        || state.resources.pixmap(request.src).is_some();
    let dst_exists = state.resources.window(request.dst).is_some()
        || state.resources.pixmap(request.dst).is_some();
    let draw_state = state.resources.resolve_draw_state(request.gc);
    let src = state.resources.host_drawable_target(request.src);
    let dst = state.resources.host_drawable_target(request.dst);
    if !gc_exists {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_GC,
            request.gc.0,
            62,
        );
    }
    if !src_exists {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_DRAWABLE,
            request.src.0,
            62,
        );
    }
    if !dst_exists {
        return emit_x11_error(
            state,
            client_id,
            sequence,
            x11::error::BAD_DRAWABLE,
            request.dst.0,
            62,
        );
    }
    if let (Some(src), Some(dst)) = (src.as_ref(), dst.as_ref()) {
        if src.depth() != dst.depth() {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_MATCH,
                request.dst.0,
                62,
            );
        }
        let st = draw_state.unwrap_or_default();
        backend.apply_clip_state(origin, &st.clip)?;
        backend.apply_draw_state(origin, &st)?;
        backend.copy_area(
            origin,
            src.host_xid(),
            dst.host_xid(),
            request.src_x,
            request.src_y,
            request.dst_x,
            request.dst_y,
            request.width,
            request.height,
        )?;
        let dst_id = request.dst;
        let _dropped = accumulate_damage_to_state(
            state,
            dst_id,
            request.dst_x,
            request.dst_y,
            request.width,
            request.height,
        );
        // X11 spec: when graphics-exposures is True, send GraphicsExpose
        // for the regions of source that weren't visible (or NoExposure
        // when fully visible). We don't track source obscurity, so we
        // conservatively emit GraphicsExpose covering the whole dest
        // to the requesting client (GraphicsExpose isn't gated by
        // ExposureMask — it always goes to the requestor).
        let graphics_exposures = state
            .resources
            .gc(request.gc)
            .is_some_and(|g| g.graphics_exposures);
        if graphics_exposures && let Some(client) = state.clients.get_mut(&client_id.0) {
            let mut buf = Vec::with_capacity(32);
            let seq = SequenceNumber(
                client
                    .last_sequence
                    .load(std::sync::atomic::Ordering::Relaxed),
            );
            x11::encode_graphics_expose_event(
                &mut buf,
                seq,
                client.byte_order,
                request.dst,
                request.dst_x as u16,
                request.dst_y as u16,
                request.width,
                request.height,
                0,
                0,
                62, // CopyArea
            );
            let _ = write_to_client(client, client_id, &buf);
        }
    }
    Ok(RequestOutcome::Handled)
}

fn handle_copy_plane(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    if body.len() < 28 {
        return Ok(RequestOutcome::Handled);
    }
    let src = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
    let dst = ResourceId(u32::from_le_bytes([body[4], body[5], body[6], body[7]]));
    let gc = ResourceId(u32::from_le_bytes([body[8], body[9], body[10], body[11]]));
    let sx = i16::from_le_bytes([body[12], body[13]]);
    let sy = i16::from_le_bytes([body[14], body[15]]);
    let dx = i16::from_le_bytes([body[16], body[17]]);
    let dy = i16::from_le_bytes([body[18], body[19]]);
    let w = u16::from_le_bytes([body[20], body[21]]);
    let h = u16::from_le_bytes([body[22], body[23]]);
    let plane = u32::from_le_bytes([body[24], body[25], body[26], body[27]]);
    if w == 0 || h == 0 {
        return Ok(RequestOutcome::Handled);
    }
    let gc_exists = state.resources.gc(gc).is_some();
    let draw_state = state.resources.resolve_draw_state(gc);
    let src_target = state.resources.host_drawable_target(src);
    let dst_target = state.resources.host_drawable_target(dst);
    if !gc_exists {
        return emit_x11_error(state, client_id, sequence, x11::error::BAD_GC, gc.0, 63);
    }
    if let (Some(srct), Some(dstt)) = (src_target, dst_target) {
        let st = draw_state.unwrap_or_default();
        backend.apply_clip_state(origin, &st.clip)?;
        backend.apply_draw_state(origin, &st)?;
        backend.copy_plane(
            origin,
            srct.host_xid(),
            dstt.host_xid(),
            sx,
            sy,
            dx,
            dy,
            w,
            h,
            plane,
        )?;
        let _dropped = accumulate_damage_to_state(state, dst, dx, dy, w, h);
        // GraphicsExpose to the requestor when graphics-exposures is True.
        let graphics_exposures = state.resources.gc(gc).is_some_and(|g| g.graphics_exposures);
        if graphics_exposures && let Some(client) = state.clients.get_mut(&client_id.0) {
            let mut buf = Vec::with_capacity(32);
            let seq = SequenceNumber(
                client
                    .last_sequence
                    .load(std::sync::atomic::Ordering::Relaxed),
            );
            x11::encode_graphics_expose_event(
                &mut buf,
                seq,
                client.byte_order,
                dst,
                dx as u16,
                dy as u16,
                w,
                h,
                0,
                0,
                63, // CopyPlane
            );
            let _ = write_to_client(client, client_id, &buf);
        }
    }
    debug!("client {} #{} CopyPlane", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

