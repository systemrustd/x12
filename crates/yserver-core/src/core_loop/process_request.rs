fn handle_big_requests_request(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
) -> io::Result<RequestOutcome> {
    if header.data != 0 {
        // Malformed minor — the reader thread parked waiting for an
        // Apply / Ignore signal after sending the Enable through the
        // channel; unblock it with `IgnoreBigRequests` so it doesn't
        // deadlock.
        if let Some(client) = state.clients.get(&client_id.0)
            && let Some(tx) = client.reader_control.as_ref()
        {
            let _ = tx.send(crate::server::ReaderControl::IgnoreBigRequests);
        }
        return Ok(RequestOutcome::Handled);
    }
    debug!("client {} #{} BigRequestsEnable", client_id.0, sequence.0);
    let Some(client) = state.clients.get_mut(&client_id.0) else {
        return Ok(RequestOutcome::Handled);
    };
    let byte_order = client.byte_order;
    client.big_requests_enabled = true;
    let mut buf: Vec<u8> = Vec::with_capacity(32);
    x11::write_big_requests_enable_reply(&mut buf, byte_order, sequence, 256 * 1024)?;
    let outcome = write_to_client(client, client_id, &buf);
    // Unblock the reader so subsequent requests use big-framing.
    if let Some(tx) = client.reader_control.as_ref() {
        let _ = tx.send(crate::server::ReaderControl::ApplyBigRequests);
    }
    Ok(outcome)
}

fn handle_get_window_attributes(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    debug!("client {} #{} GetWindowAttributes", client_id.0, sequence.0);
    let id = x11::drawable_request_id(body).unwrap_or(ROOT_WINDOW);
    // Spec: BadWindow on unknown window ID. Don't silently fall back to
    // ROOT_WINDOW — xts probes stale/destroyed XIDs and expects the
    // protocol error.
    if state.resources.window(id).is_none() {
        return emit_x11_error(state, client_id, sequence, x11::error::BAD_WINDOW, id.0, 3);
    }
    let target = id;
    let your_event_mask = state
        .clients
        .get(&client_id.0)
        .and_then(|c| c.event_masks.get(&target).copied())
        .unwrap_or(0);
    let all_event_masks: u32 = state
        .clients
        .values()
        .filter_map(|c| c.event_masks.get(&target).copied())
        .fold(0u32, |a, b| a | b);
    let attrs = window_attributes(
        state.resources.window(target),
        all_event_masks,
        your_event_mask,
    );
    let Some(client) = state.clients.get_mut(&client_id.0) else {
        return Ok(RequestOutcome::Handled);
    };
    let byte_order = client.byte_order;
    let mut buf: Vec<u8> = Vec::with_capacity(48);
    x11::write_get_window_attributes_reply(&mut buf, byte_order, sequence, attrs)?;
    Ok(write_to_client(client, client_id, &buf))
}

fn window_attributes(
    window: Option<&Window>,
    all_event_masks: u32,
    your_event_mask: u32,
) -> x11::WindowAttributes {
    let window = window.expect("root window exists");
    // X11 protocol defaults for unset attributes — see "Window Attributes"
    // in the core spec. The Window struct doesn't yet store
    // bit_gravity / backing-store / backing-planes / backing-pixel /
    // save-under / colormap / do-not-propagate-mask, so we surface the
    // spec defaults here. ChangeWindowAttributes accepts the value-
    // mask but doesn't persist these fields yet — separate task.
    x11::WindowAttributes {
        visual: window.visual,
        class: window.class.protocol_value(),
        bit_gravity: 0, // ForgetGravity (default)
        win_gravity: 1, // NorthWestGravity (default)
        backing_planes: u32::MAX,
        backing_pixel: 0,
        save_under: false,
        map_is_installed: true,
        map_state: window.map_state.protocol_value(),
        override_redirect: window.override_redirect,
        colormap: ROOT_COLORMAP,
        all_event_masks,
        your_event_mask,
        do_not_propagate_mask: 0,
    }
}

fn handle_circulate_window(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    header: RequestHeader,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    if body.len() < 4 {
        debug!(
            "client {} #{} CirculateWindow (short body)",
            client_id.0, sequence.0
        );
        return Ok(RequestOutcome::Handled);
    }
    let container = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
    let direction = header.data;
    let chosen_child = {
        let kids = state.resources.children(container);
        match (direction, kids.first(), kids.last()) {
            (0, _, Some(&back)) => Some(back),
            (1, Some(&front), _) => Some(front),
            _ => None,
        }
    };
    let Some(child) = chosen_child else {
        return Ok(RequestOutcome::Handled);
    };
    // SubstructureRedirect on container takes priority — redirect the
    // request to the first subscriber instead of performing the
    // circulate.
    let redirect_target = subscribers_by_id(state, container, 0x0010_0000)
        .into_iter()
        .next();
    if let Some(target) = redirect_target {
        let _dropped = fanout_event_to_clients(state, &[target], |buf, seq, order| {
            let _ =
                x11::write_circulate_request_event(buf, order, seq, container, child, direction);
        });
    } else {
        let _ = state.resources.circulate_window(container, direction);
        let on_child = subscribers_by_id(state, child, 0x0002_0000);
        let on_container = subscribers_by_id(state, container, 0x0008_0000);
        let mut targets = on_child;
        for cid in on_container {
            if !targets.contains(&cid) {
                targets.push(cid);
            }
        }
        let _dropped = fanout_event_to_clients(state, &targets, |buf, seq, order| {
            let _ = x11::write_circulate_notify_event(buf, order, seq, child, child, direction);
        });
    }
    debug!("client {} #{} CirculateWindow", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

fn handle_create_cursor(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    origin: Option<OriginContext>,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    if body.len() >= 28 {
        let cursor_id = ResourceId(u32::from_le_bytes([body[0], body[1], body[2], body[3]]));
        if state.resources.xid_in_use(cursor_id)
            || xid_out_of_client_range(state, client_id, cursor_id.0)
        {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_ID_CHOICE,
                cursor_id.0,
                93,
            );
        }
        let source_id = ResourceId(u32::from_le_bytes([body[4], body[5], body[6], body[7]]));
        let mask_id = ResourceId(u32::from_le_bytes([body[8], body[9], body[10], body[11]]));
        let fore = (
            u16::from_le_bytes([body[12], body[13]]),
            u16::from_le_bytes([body[14], body[15]]),
            u16::from_le_bytes([body[16], body[17]]),
        );
        let back = (
            u16::from_le_bytes([body[18], body[19]]),
            u16::from_le_bytes([body[20], body[21]]),
            u16::from_le_bytes([body[22], body[23]]),
        );
        let hot_x = u16::from_le_bytes([body[24], body[25]]);
        let hot_y = u16::from_le_bytes([body[26], body[27]]);

        let src_host = state.resources.pixmap(source_id).and_then(|p| p.host_xid);
        let mask_host = if mask_id.0 == 0 {
            None
        } else {
            state.resources.pixmap(mask_id).and_then(|p| p.host_xid)
        };
        state.resources.create_cursor(client_id, cursor_id);
        if let Some(src_host) = src_host {
            match backend.create_cursor(origin, src_host, mask_host, fore, back, hot_x, hot_y) {
                Ok(handle) => {
                    state.resources.set_cursor_host_xid(cursor_id, handle);
                }
                Err(err) => {
                    log::warn!("client {} CreateCursor failed: {err}", client_id.0);
                }
            }
        }
    }
    debug!("client {} #{} CreateCursor", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

fn handle_create_glyph_cursor(
    state: &mut ServerState,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    if let Some(cursor) = x11::create_glyph_cursor_id(body) {
        let new_id = cursor.0;
        let validation_failed = {
            let handle = state.clients.get(&client_id.0).expect("client registered");
            let owned = crate::server::IdAllocator::validate_owned(
                new_id,
                handle.resource_id_base,
                handle.resource_id_mask,
            );
            let in_use = state.resources.any_resource_exists(cursor);
            !owned || in_use
        };
        if validation_failed {
            return emit_x11_error(
                state,
                client_id,
                sequence,
                x11::error::BAD_ID_CHOICE,
                new_id,
                94,
            );
        }
        state.resources.create_glyph_cursor(client_id, cursor);
    }
    debug!("client {} #{} CreateGlyphCursor", client_id.0, sequence.0);
    Ok(RequestOutcome::Handled)
}

fn handle_query_extension(
    state: &mut ServerState,
    backend: &mut dyn Backend,
    client_id: ClientId,
    sequence: SequenceNumber,
    body: &[u8],
) -> io::Result<RequestOutcome> {
    let name = x11::query_extension_name(body);
    let (present, major_opcode, first_event, first_error) = extension_query_reply(&name, backend)
        .map(|(major_opcode, first_event, first_error)| {
            (true, major_opcode, first_event, first_error)
        })
        .unwrap_or((false, 0, 0, 0));
    debug!(
        "client {} #{} QueryExtension {:?} -> {}",
        client_id.0,
        sequence.0,
        name,
        if present { "present" } else { "absent" }
    );
    let Some(client) = state.clients.get_mut(&client_id.0) else {
        return Ok(RequestOutcome::Handled);
    };
    let byte_order = client.byte_order;
    let mut buf: Vec<u8> = Vec::with_capacity(32);
    x11::write_query_extension_reply(
        &mut buf,
        byte_order,
        sequence,
        present,
        major_opcode,
        first_event,
        first_error,
    )?;
    Ok(write_to_client(client, client_id, &buf))
}

